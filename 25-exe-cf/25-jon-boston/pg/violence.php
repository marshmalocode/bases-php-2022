        <p>Le FBI a publié jeudi des statistiques sur plus de 11 millions d'infractions pénales signalées à une base de données nationale par les services de police, petits et grands. Les meurtres ont augmenté aux États-Unis et les taux de vols ont diminué de 2020 à 2021.
        <p>Les vols, les homicides et les voies de fait graves ont diminué d'une année sur l'autre à Boston, tandis que le nombre d'incidents de viol signalés a augmenté, selon les données du FBI. La criminalité violente dans le Massachusetts a diminué de moins de 1% entre 2020 et 2021, une tendance conforme aux données nationales.</p>

        <p>Le département de police de Boston a signalé 3885 crimes au total en 2021, contre 4 354 en 2020, soit une baisse d'environ 10%. Le département a signalé 34 homicides en 2021 contre 54 en 2020 et 233 viols en 2021 contre 181 en 2020.</p>
        <p>919 autres vols qualifiés ont été signalés en 2020, contre 774 en 2021, tandis que le département a signalé au FBI 2442 agressions graves en 2021, contre 2745 en 2020.</p>

        <p>Les meurtres et les vols ont chuté en 2021 dans le Massachusetts, alors que les viols et les agressions ont augmenté, selon les données du FBI</p>

        <p>Un porte-parole de la police de Boston n'a pas immédiatement renvoyé une demande de commentaire.</p>

        <p>Les services de police des campus locaux communiquent également des statistiques à la base de données nationale du FBI. Le département de police de l'Université de Boston a signalé neuf incidents criminels en 2021 contre 10 en 2020, tandis que le département de police du Boston College a signalé deux crimes en 2021 contre un en 2020.</p>

        <p>L'ensemble de données du FBI manque de détails sur certains des plus grands départements du pays, comme le département de police de New York et le département de police de Los Angeles. Environ 40% des forces de l'ordre du pays n'ont pas soumis de statistiques sur la criminalité après une refonte du système de signalement du FBI.</p>


        <p>Les données incomplètes pourraient être un terrain propice pour que les politiciens et les groupes d'activistes utilisent les chiffres pour faire avancer leurs arguments. Mais avec tant de grandes villes qui ne signalent même pas un seul crime, les experts disent que le tableau de la criminalité aux États-Unis reste flou.</p>

        <p>L'augmentation qui a commencé en 2020 défie toute explication facile. Les experts soulignent plusieurs facteurs potentiels: la pandémie qui a tué plus d'un million de personnes aux États-Unis, la violence armée, les inquiétudes concernant l'économie, les taux d'inflation élevés et le stress intense.</p>

