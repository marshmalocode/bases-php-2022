    <h2 class="ldc1">Quelle est l’histoire du football ?</h2>
<p class="text2">Le football est sans aucun doute le plus grand sport au monde car il est le plus pratiqué, devant le cricket, le basket, le tennis et le hockey. Presque tous les pays y jouent, et plus de 250 millions de joueurs sont licenciés dans le monde. Sa popularité provient sans doute de sa simplicité : il suffit après tout d’un ballon et d’une bonne paire de chaussures pour jouer.  La création du football est une fabuleuse histoire mais aussi une histoire populaire qui n’a cessé de s’enrichir au fil des années, et que nous allons vous raconter.</p>

 

<h2 class="ldc1">Qui a inventé le football et en quelle année ?</h2>

<p class="text2">Le football moderne a été inventé en Angleterre au 19e siècle, mais le jeu de ballon a des origines bien plus anciennes. Le jeu de kemari issu d’un sport chinois puis popularisé au Japon au 7e siècle serait le véritable ancêtre du football. Il s’agissait d’un jeu de jongle avec la jambe droite, qui consistait à maintenir le ballon en l’air le plus longtemps possible sans le faire tomber.<br>

 

<br>On a identifié d’autres variantes assez semblables dans le monde entier, comme le Pasuckuakohowog chez les Indiens d’Amérique, le Marngrook chez les Aborigènes d’Australie et le Ki-o-rahi chez les Maori de Polynésie. Le football tirerait aussi ses origines de la soule, un jeu du Moyen-Age qui se pratiquait autant avec les pieds qu’avec les mains, et qui aurait également inspiré le handball et le rugby.<br>

 

<br>Le jeu de ballon était pratiqué en Angleterre comme partout ailleurs, mais avec des règles très variables selon les écoles et les régions. Dès 1843, l'Université de Cambridge propose une tentative de normalisation et de codification des règles du jeu. Les « règles de Cambridge » de 1948 constituent le première règlement officiel du football. Elles stipulent entre autres que le jeu se joue uniquement avec les pieds, que le ballon doit être posé au milieu du terrain en début de partie, qu’une touche est accordée en cas de sortie du terrain, qu’une faute est sanctionnée d’un coup franc et qu’un but est marqué lorsque le ballon franchit la ligne délimitée par deux drapeaux posés de part et d’autre du terrain.<br>

 

<br>La date de naissance officielle du football est donc 1848, bien que les règles édictées ne furent  pas tout de suite appliquées dans toute l’Angleterre. C’est en 1863 qu’un groupe de 11 écoles et clubs de Londres adoptent un règlement commun, et en 1877 que la Football Association fraichement créée convient d’un code uniforme respecté par les 43 clubs du pays.<br>

 

<br>Le premier match officiel de football s’est par ailleurs déroulé en 1860 et il opposait le Sheffield FC au Hallam FC (deux villes de Sheffield, au nord de l’Angleterre). C’est en 1866 que la durée d’un match de football est fixée à 90 minutes, et en 1870 que la règle des deux équipes de onze joueurs sur le terrain est définitivement adoptée.<br>

 

<br>Le football s’est ensuite rapidement internationalisé, notamment sous l’influence d’immigrants britanniques en Amérique du Sud, par la création de fédérations et d’associations au Brésil, en Argentine, en France et en Hollande.<br>

 

<h3 class="ldc1">Quel est le but du football ?</h3>
<p class="text2">Le but ultime du football est de marquer… des buts ! L’objectif principal est donc d’envoyer un maximum de ballon dans les cages de l'équipe adverse jusqu’à ce que la fin du match soit sifflée. Pour ce faire, l’équipe est composée d’un gardien de but et de 10 joueurs de champ occupant des fonctions plus spécifiques de défenseurs, de milieux de terrain ou d’attaquants.

 

Le football est populaire car ses règles sont simples à appliquer (pour les joueurs) et à comprendre (pour les spectateurs). Mais c’est aussi un jeu spectaculaire qui demande beaucoup d’adresse et de rapidité. La balle n’est pas facile à contrôler à pleine vitesse, et bien maitriser un match nécessite d’appliquer de fines stratégies de collaboration avec ses coéquipiers.

 

Le but du football est d’être aussi agréable à jouer qu’à regarder, d’être capable d’enthousiasmer des foules de spectateurs dans le monde entier mais aussi de pouvoir être joué n’importe où (en salle ou en extérieur, sur un sol en dur ou du gazon…) et de multiples façons (à 2 contre 2, 5 contre 5, 7 contre 7…).</p>
