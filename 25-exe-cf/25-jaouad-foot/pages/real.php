    <h2 class="barca3">Histoire du Real Madrid</h2>
    <p class="barca3"><br>Le Real Madrid Club de Fútbol, plus connu sous le nom de Real Madrid ou simplement Real, est un club professionnel espagnol de football, basé à Madrid. Vainqueur de très nombreux titres nationaux et internationaux, il a reçu de la Fédération internationale de football association (FIFA) le titre honorifique de plus grand club du xxe siècle.<br>

    <br>Fondé en 1902 par Julián Palacios et les frères Juan et Carlos Padrós, le Madrid Football Club se voit adjoindre en 1920 le titre Real (signifiant « royal » en espagnol) par le roi Alphonse XIII et son nom est hispanisé en 1941 pour devenir Real Madrid Club de Fútbol. Le Real Madrid s'impose au sommet du football européen au milieu des années 1950, sous la présidence de Santiago Bernabéu, en remportant notamment les cinq premières éditions de la Coupe d'Europe des clubs champions. Dans les années 1980, le club possède à nouveau l'une des meilleures équipes d'Europe, surnommée La Quinta del Buitre (« la bande du vautour »), qui remporte notamment deux fois consécutivement la Coupe de l'UEFA. Le Real Madrid renoue au début des années 2000 avec sa politique de recruter les meilleurs joueurs du monde, ce qui vaut à son équipe le surnom de « Galactiques ».<br>

    <br>À domicile, le club évolue habituellement tout en blanc, ce qui lui vaut le surnom de los Blancos (« les blancs »). Le club évolue depuis 1947 au Stade Santiago Bernabéu, situé dans le centre de Madrid, dont la capacité est de 81 044 places depuis sa dernière rénovation en 2006.<br>

    <br>Le Real Madrid entretient des rivalités de longue date avec certains clubs, notamment le FC Barcelone (la confrontation entre les deux clubs, surnommée El Clásico, est l'une des plus célèbres de l'histoire du football) et avec l'autre grand club de la capitale espagnole l'Atlético de Madrid dans le derby madrilène.<br>

    <br>Contrairement à la plupart des clubs européens, le Real Madrid est la propriété collective de ses « socios » (ses sociétaires), qui le gèrent depuis sa création. Pourtant, le club est celui qui génère le plus de revenus au monde.<br></p>

