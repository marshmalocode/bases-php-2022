    <h2 class="messi1">Biographie</h2>
   
   <p class="messi1"><br>Lionel Messi naît le 24 juin 1987 à Rosario en Argentine. Dès l’âge de 5 ans, il joue au football dans un petit club de sa ville. Très talentueux, le jeune garçon rencontre pourtant de sérieux obstacles lorsqu’il connaît des problèmes de santé. Une maladie hormonale l’empêche de grandir et compromet sa possible carrière. Après deux années de traitement, il a suffisamment grandi et intègre l’équipe des jeunes de Barcelone.<br>
   
   <br>Il rejoint par la suite le FC Barcelona et devient très vite indispensable au club. Malgré son mètre 69, il dribble comme personne et son talent éblouit les supporters. Auteur de 400 buts, Lionel Messi est le seul joueur du monde à être quintuple ballon d’or et il est le premier buteur de l’Argentine. Le recordman parvient à mener son équipe en finale de la Coupe du monde 2014 face à la Mannschaft. La reconnaissance du footballeur est telle qu’un néologisme emprunté à son nom a intégré le dictionnaire espagnol; imessionante. L’adjectif désigne la manière extraordinaire de l’attaquant de jouer au foot. En août 2015, il est élu Meilleur joueur européen par l'UEFA. En janvier 2016, il remporte le Ballon d'or 2015.<br>
   
   <br>Le 5 août 2021, le club du FC Barcelone officialise le non-renouvellement du contrat de Lionel Messi. Quelques jours plus tard, le 10 août 2021, le joueur signe avec le PSG. Le 29 novembre 2021, il remporte le Ballon d'Or 2021, le septième de sa carrière.<br>
   
   <br>Côté vie privée, Lionel Messi est père de deux garçons, Thiago (né en novembre 2012) et Matteo (né en septembre 2015) avec sa compagne Antonella Roccuzzo. Le 30 juin 2017, le couple se marie en Argentine. En octobre 2017, le couple révèle attendre un troisième enfant. Le 10 mars 2018, la famille s'agrandit avec la naissance d'un troisième garçon prénommé Ciro.</p><br>
   
