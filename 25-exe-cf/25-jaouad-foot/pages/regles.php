<h2 class="regles1">LOI 1 – NOMBRE DE JOUEURS</h2>
<p class="regles1">Il y a 11 joueurs dont un est gardien de but. Chaque équipe doit être composée au moins de 7
joueurs pour participer au match (compétitions officielles FIFA). Il est possible de recourir au
maximum à trois remplaçants (le nombre peut changer selon les compétitions).
Le remplaçant ne pénètre sur le terrain qu’après la sortie du joueur qu’il remplace et après y
avoir été invité par un signe de l’arbitre.
Le remplacement s’effectue au niveau de la ligne médiane.
Un joueur qui a été remplacé ne peut plus participer au match.
Le remplacement s’effectue pendant un arrêt du jeu.</p>
<h2 class="regles1">LOI 2 – EQUIPEMENT DE JOUEURS</h2>
<p class="regles1">Short, maillot, chaussettes, protège-tibias, chaussures
Les protège-tibias doivent obligatoirement être recouverts par les chaussettes, le maillots doit
être dans le short (afin d’éviter au maximum le tirage de maillot, et se rendre rapidement s’il y
a eu tirage de maillot).
Les chaussures ne doivent pas forcément avoir de crampons, si elles ont ceux-ci doivent être
cylindriques.</p>
<h2 class="regles1">LOI 3 – ARBITRE</h2>
<p class="regles1">Un match ne peut s’effectuer sans arbitre.
- il doit veiller à l’application des lois du jeu
- il assure le contrôle du match en collaboration avec les arbitres assistants, le cas
échéant, avec le quatrième
- il peut arrêter temporairement le match (en fonction des infractions au lois du jeu)
ou définitivement (en raison de l’interférence d’événements extérieures) </p>
<h2 class="regles1">LOI 4 – ARBITRES ASSISTANTS</h2>
<p class="regles1">Au nombre de deux, ils ont pour mission de signaler :
- lorsque le ballon sort du terrain et à qui le ballon doit revenir
- lorsqu’un joueur doit être sanctionné de hors-jeu
- quand un comportement répréhensible ou tout autre incident est survenu en dehors
du champ de vision de l’arbitre
- quand des fautes sont commises lorsque les arbitres assistants sont plus près de
l’action que l’arbitre
- signale si le ballon a bien franchi la ligne lors d’un coup de pied de réparation</p>
<h2 class="regles1">LOI5 5 – DUREE DU MATCH</h2>
<p class="regles1">Le match se compose de deux périodes de 45 minutes. La pause de la mi-temps ne doit pas
excéder 15 minutes. Chaque période doit être prolongée pour récupérer tout le temps perdu
occasionné par : les remplacements, les blessures et toute autre cause</p>
<h2 class="regles1">LOI 6 – COUP D’ENVOI ET REPRISE DU JEU</h2>
<p class="regles1">Le choix du camp est déterminé par un tirage au sort effectué au moyen d’une pièce de
monnaie. Il y a un coup d’envoi au début de chaque période (également pour les
prolongations) et après un but marqué.
Lors d’un coup d’envoi, le ballon est en jeu lorsqu’il est botté vers l’avant.</p>
<h2 class="regles1">LOI 7 – BALLON EN JEU ET HORS DU JEU</h2>
<p class="regles1">Le ballon est hors du jeu quand :
- il a entièrement franchi la ligne de but ou la ligne de touche qu’il soit à terre ou en
l’air
- le jeu a été arrêté par l’arbitre</p>
<h2 class="regles1">LOI 8 – HORS JEU</h2>
<p class="regles1">Position de hors jeu : un joueur se trouve en position de hors jeu quand :
- il est plus près de la ligne de but adverse qu’à la fois le ballon et
l’avant dernier adversaire
4
Un joueur ne se trouve pas en position hors-jeu quand :
- il se trouve dans sa propre moitié de terrain ou
- il se trouve à la même hauteur que l’avant dernier adversaire ou
- il se trouve à la même hauteur que les deux derniers défenseurs
La position de hors jeu d’un joueur ne doit être sanctionné que si, au moment où le ballon est
touché par un coéquipier ou est joué par l’un d’eux, le joueur prend, de l’avis de l’arbitre, une
part active au jeu :
- en intervenant dans le jeu ou
- en influençant un adversaire ou
- en tirant un avantage de cette position
Il n’y a pas de hors-jeu quand un joueur reçoit le ballon directement :
- sur coup de pied de but ou
- sur une rentrée de touche
- sur un coup de pied de coin
Lorsqu’un hors-jeu est sanctionné, l’arbitre accorde à l’équipe adverse un coup franc direct
qui doit être exécuté à l’endroit ou la faute a été commise.<p>
