      <h2>Antiquité</h2>
      <p>L'idée neutre de « secte » (au sens d'« école de pensée », « tendance ») s'exprime en grec ancien par le terme
        hairesis (αἵρεσις : option, opinion particulière, groupe doctrinal, parti), qui a pour équivalent le latin
        secta15. Le mot hérésie désignait initialement le choix ou la préférence pour une doctrine, avant d'acquérir la
        connotation péjorative que l'Église catholique lui associa : celle de doctrine dissidente, voire égarée16.</p>

      <p>Plusieurs grandes religions actuelles, à l'origine, furent des sectes issues de religions plus anciennes et
        déjà bien implantées. Ainsi, le christianisme s'est d'abord développé comme une évolution au sein de la
        nébuleuse des partis du judaïsme17. Néanmoins, ces religions ont acquis au fil du temps un grand nombre
        d'adeptes et une reconnaissance officielle importante.</p>

      <p>L'Antiquité ne donnait pas de connotation négative aux termes « secte » ou « hérésie », mais faisait en
        revanche une distinction entre religion licite ou non. C'est en tant que religion illicite que les croyances et
        les usages des premiers chrétiens, au début du iie siècle, furent qualifiés par Pline le Jeune de « superstition
        déraisonnable et sans mesure »18. De même, mais avec plus de sévérité et surtout avec une animosité absente chez
        Pline, le polémiste hellénophone Celse, dans son pamphlet rédigé vers 178, accusa les chrétiens de vouloir miner
        l'ordre social et former un État dans l'État ; entre autres griefs, il leur reprocha de nuire à la santé
        publique en détournant le peuple de recourir aux médecins reconnus et en leurrant les naïfs par d'illusoires
        promesses de guérison19.</p>

      <h2>Acception péjorative dans l'Empire romain chrétien</h2>
      <p>C'est dans le sens de sous-branche de religion que l'on peut parler de « sectes » à propos des groupements
        issus du bouddhisme, de l'hindouisme, du shintoïsme ou du taoïsme. Mais autant ces religions n'affichent pas de
        politique particulière envers la dissidence, autant le christianisme, se voulant par nature universel, a mis en
        garde contre la multiplicité des écoles, avant de la proscrire et d'user, à son encontre, de procédés coercitifs
        ou radicaux20 : dans son aire de domination, toute dissidence, qualifiée tantôt d'hérésie, tantôt de schisme, a
        donc eu une connotation péjorative, et a été susceptible de répression. Constantin Ier, puis Théodose le Grand,
        soucieux de consolider l'unité de l'Empire romain, redéfinirent la notion d'orthodoxie en affirmant que le
        magistère doctrinal de l'Église chrétienne et l'autorité souveraine du pouvoir impérial étaient interdépendants.
        L'acception positive du mot secta fit les frais de cette théologie politique : la législation impériale du
        Bas-Empire chrétien criminalisa le libre choix en matière religieuse, et le terme secta devint dès lors un
        quasi-synonyme des mots haeresis et schisma21.</p>

      <h2>Application au protestantisme</h2>
      <p>Le terme « secte protestante » entre dans le langage courant par la porte de la polémique antiprotestante et de
        l'apologétique catholique. En effet, à partir de l'excommunication de Luther en 1521, l'Église catholique
        romaine considère le protestantisme comme une hérésie, ce qui entraîna d'ailleurs une longue série de conflits
        meurtriers aux xvie et xviie siècles et de nombreux massacres, les plus connus étant celui de la
        Saint-Barthélemy, le 24 août 1572 et celui de la mise à sac de Magdebourg le 20 mai 1631.</p>

      <p>
        C'est le prédicateur et apologète Jacques-Bénigne Bossuet qui popularise la notion de « secte protestante », qui
        devient monnaie courante à partir de la publication en 1688 de son « Histoire des variations des Églises
        protestantes ». Dans cet ouvrage qui connaît un grand retentissement, Bossuet tire adroitement argument de la
        diversité des protestants pour affirmer qu'il ne peut s'agir de la « vraie foi ». Puis c'est l'érudit protestant
        réfugié aux Pays-Bas Pierre Bayle, qui, multiplie par souci encyclopédique, dans son monumental « Dictionnaire
        historique et critique » paru en 1697 et réédité 8 fois au cours du xviie siècle, les articles sur des
        groupuscules parfois déjà disparus ou sur de simples tendances indûment transformées en dénominations
        constituées, avec de plus bon nombre de doublons dus à la pluralité des appellations. Il va ainsi renforcer
        auprès des savants du xviiie siècle cette notion d'émiettement du protestantisme en sectes, en dépit du fait
        qu'on pourrait aussi établir une longue liste des sectes issues du catholicisme22. </p>

      <h2>Âge des Lumières et xixe siècle</h2>
      <p>Dans son Dictionnaire philosophique, Voltaire classe comme sectaire toute croyance non acceptée universellement
        ou non prouvée de façon évidente23.</p>

      <p>La notion de secte possède donc un caractère relatif pouvant évoluer au cours du temps. Comme le veut une
        formule célèbre mais douteuse et fort malmenée (certains l'attribuent faussement à Ernest Renan)24, « qu'est-ce
        qu'une Église (variante : une religion), sinon une secte qui a réussi ? ». Robert Schroeder — un des fondateurs
        de l'association chrétienne Vigi-Sectes, professeur, auteur et conférencier — affirme que la chrétienté actuelle
        apparaît comme une mosaïque de plus de cinq cents sectes, affirmant toutes être l’authentique Église de
        Jésus-Christ25.</p>

      <h2>Époque contemporaine</h2>
      <p>Selon Arnaud Esquerre, « la secte en tant qu’organisation totalitaire, à visée thérapeutique, spirituelle,
        philosophique ou religieuse, et au sein de laquelle les adeptes sont manipulés mentalement, est une invention
        française et collective des années 1970 et 1980 »26.</p>
      <p>Dans la seconde moitié du xxe siècle apparaissent de nouveaux mouvements (appelés nouveaux mouvements religieux
        par certains sociologues) qui ne correspondent plus à la typologie webero-troeltschienne. Comme causes possibles
        de leur émergence, on cite la baisse de fréquentation des religions traditionnelles, le désenchantement du
        monde27, et l'effondrement d'idéologies comme le communisme, qui amènent à une perte de valeurs et de repères28.
        Par ailleurs, certains sociologues et théologiens estiment que le phénomène de mondialisation a permis
        l'apparition d'un véritable « supermarché du religieux »29 où le choix des croyances est plus vaste.</p>
      <p>Dans les années 1980, à la suite de scandales qui ont alarmé l'opinion publique, tels que suicides collectifs,
        affaires politico-financières, polygamie, sorcellerie, ou exercice illégal de la médecine, le terme « secte »,
        utilisé pour désigner certains de ces mouvements, a pris une forte connotation péjorative, devenant synonyme de
        groupe totalitaire et dangereux, ou en tous cas, de système aliénant et forçant ses adeptes à se placer en
        position de rupture avec la société et ses normes.</p>
      <p>Récemment, certains de ces mouvements investissent le créneau du développement personnel et de la
        psychothérapie30. La Miviludes, dans son rapport de 200931, tire la sonnette d'alarme sur les psychothérapeutes
        sectaires et contribue en France à la régulation de la profession en juillet 201032.</p>
