      <h2>Diversité des points de vue sur les sectes</h2>
      <p>Les polémiques autour des sectes sont souvent alimentées par l'ambivalence de ce mot, déjà signalée plus haut.
        Outre le sens étymologique et premier, d'école spirituelle ou communauté qui soit suit fidèlement l'enseignement
        d'un maître (acception primitive et neutre), soit est entrée en dissidence d’une religion installée (acception
        susceptible d'une connotation dépréciative), on distingue :

        un sens positif revendiqué, depuis la fin du xxe siècle, par les nouveaux mouvements religieux : groupe
        d'individus libres exerçant ensemble une activité dans un champ religieux ou spirituel, comme d'autres
        s'associent dans un domaine artistique, avec son système de croyances ou sa philosophie originale, plus ou moins
        perfectionné et des adeptes, apparemment, non manipulés mentalement ;
        un sens négatif « fort » (répandu depuis les années 1970) : toute organisation, y compris les sociétés secrètes,
        ayant été condamnée pour préjudices envers ses adeptes, manipulés mentalement ou ayant subi d'autres contraintes
        ;
        un sens négatif « étendu » (quasi contemporain du précédent) : toute organisation soupçonnée d’exercer une
        manipulation mentale sur ses adeptes afin de les exploiter. Les deux acceptions négatives du terme sont à
        présent celles adoptées par les médias et par la population à leur suite.
        Par rapport aux sectes, les positions vont dépendre des convictions religieuses et philosophiques sous-jacentes
        des intervenants :

        les militants antireligieux auront tendance à minimiser toute différence entre le terme secte et le terme
        religion (ou spiritualité) et emploieront parfois le sens négatif étendu. Ainsi, des communautés appartenant à
        des religions installées sont considérées comme des « sectes » (au sens péjoratif du terme) par les mouvements
        antisectes ainsi que par les médias comme la Communauté Saint-Jean44 chez les catholiques.
        les défenseurs des grandes religions auront, pour certains, tendance à adopter le sens négatif fort, afin de
        désigner par secte tous les mouvements qu’ils jugent dangereux (et seulement ceux-là) et, pour d'autres
        situations, appliqueront de manière générale le sens de nouveau mouvement religieux, plus valorisant pour eux.
        Selon l'historienne belge Anne Morelli45, les grandes religions présentent des caractéristiques qui peuvent
        laisser penser qu'elles ne diffèrent pas essentiellement des mouvements sectaires. D'autres auteurs apportent un
        point de vue différent, en considérant que les « grandes religions » ne peuvent être assimilées aux sectes, en
        tout ou en partie, parce qu'elles sont reconnues, admises et intégrées à la société46. Toujours selon Anne
        Morelli, c'est le label décerné par le gouvernement du pays qui les héberge qui donnerait aux groupes religieux
        la qualification de secte ou non47.
        les défenseurs des libertés spirituelles choisiront également, indépendamment de leurs propres convictions
        religieuses, de limiter le sens négatif fort à partir de critères objectifs (jugements des tribunaux par
        exemple), afin d’éviter que la dénomination secte n'entraîne une méfiance injustifiée vis-à-vis de groupes
        religieux ou philosophiques qui ne pratiquent apparemment pas la manipulation mentale, ou pas plus, au
        demeurant, que n’importe quel groupement humain.
        L'appellation de secte au sens négatif étendu, est fondée sur la notion de manipulation mentale, difficile à
        identifier et, plus particulièrement, à distinguer de l’endoctrinement religieux48.</p>
      <h2>Dans l'hindouisme</h2>
      <p>Dans son livre sur l'hindouisme, l'indianiste Axel Michaels explique que le mot « secte », dans un contexte
        indien, ne désigne pas une rupture avec la communauté ou une exclusion, mais plutôt « une tradition organisée,
        généralement établie par les fondateurs avec des pratiques ascétiques ». Selon Michaels, « les sectes indiennes
        ne se concentrent pas sur l'hérésie — que l'absence d'un centre obligatoire rend, de fait, impossible —, mais
        mettent plutôt l'accent sur les adhérents et les adeptes »49.</p>
      <h2>Dans le christianisme</h2>
      <p>À l'origine, le christianisme apparaît comme une secte juive50,51. Divers mouvements récents issus d'un réveil
        religieux sont parfois considérés, du fait de leurs positions différentes et du petit nombre de leurs adeptes,
        comme des sectes par les autres mouvements chrétiens. L'historien Émile-Guillaume Léonard a fait remarquer en
        1954 que ce critère numérique a conduit l’opinion publique en France à utiliser le mot « secte » pour le
        baptisme en raison de la situation minoritaire du mouvement dans le pays, alors qu’aux États-Unis la situation
        majoritaire du mouvement ne permettait pas le même scénario22. Cette attitude du groupe chrétien majoritaire
        vis-à-vis des groupes minoritaires est moins présente dans les pays où le pluralisme religieux est ancré dans la
        société, comme en Grande-Bretagne et aux États-Unis52. Selon le rapport 2016-2017 de la Miviludes, en France, le
        deuxième mouvement religieux qui comptait le plus de signalements pour de présumées dérives sectaires était le
        christianisme évangélique, et majoritairement dans le courant pentecôtiste pour des églises non membres du
        Conseil national des évangéliques de France53,54. En 2020, le mouvement a eu plus de 200 signalements,
        majoritairement dans le courant néo-charismatique55,56.</p>
      <h2>Dans le bouddhisme</h2>
      <p>Le mot secte désignait autrefois, dans les études universitaires portant sur le bouddhisme, les différents
        groupes ou écoles qui suivaient les enseignements de Gautama Bouddha et ne signifiait pas « secte » dans le sens
        qui lui est actuellement donné, mais plutôt « sangha » (en sanskrit) qui veut dire une communauté de
        pratiquants. À tort ou a raison, un certain nombre de nouveaux mouvements religieux d'inspiration bouddhiste,
        regroupés au Japon sous le qualificatif de Shinshūkyō ont reçu le qualificatif de « sectes » dans le sens
        conventionnel qui lui est actuellement donné.

        Les tibétologues françaises Anne-Marie Blondeau et Anne Chayet font cependant remarquer que le terme de secte a
        une connotation péjorative ou négative en français57.</p <h2>Scandales et médiatisation</h2>
      <p>Des affaires de suicides collectifs ou d'agressions sexuelles ont défrayé la chronique depuis 1978 entraînant
        une importante médiatisation du phénomène et provoquant de profondes inquiétudes.</p>
      <p>Les sectes sont particulièrement nombreuses en Corée du Sud (une centaine, dont certaines comptent des dizaines
        de milliers de fidèles) et sont régulièrement mêlées à des scandales : en 2018, Lee Jaerock, gourou de l’Église
        de Manmin (12 000 membres), a été condamné pour viol ; l'année suivante, Shin Ok-ju, prophétesse de la secte de
        la Voie de la grâce, est condamnée pour détention et torture de 400 fidèles. Le plus haut niveau de l’État est
        également concerné : Choi Soon-sil, proche de la présidente Park Geun-hye (toutes deux arrêtées en 2018 pour
        détournement de fonds publics), était la fille d'un autoproclamé prophète qui eut une grande influence sur la
        future cheffe de l’État lorsqu’elle était jeune. En 2020, l'Église Shincheonji de Jésus est mise en cause pour
        avoir contribué à propager l'épidémie de Covid-19 dans le pays en refusant de respecter les mesures
        sanitaires58.

        La forte médiatisation du « phénomène sectaire » a pu conduire l'opinion publique à amalgamer entre elles des
        organisations aux aspirations pourtant très différentes. Ainsi, par le même terme générique de « secte », on a
        désigné autant des groupes criminels, dangereux, pédophiles, polygames, et totalitaires que des nouveaux
        groupements religieux, ésotériques ou simplement excentriques, en décalage avec la société, qui n'ont jamais été
        coupables de tels actes.

        Si cette médiatisation a eu pour effet de mettre en lumière des abus au sein de certains groupements, elle a
        parfois été très préjudiciable à des personnes ou des organisations59.

        Les défenseurs des mouvements dits sectaires mettent en avant le fait que certaines personnes, à la suite de la
        découverte de leur appartenance à des groupements considérés comme sectaires, ont été licenciées, se sont vu
        refuser la garde d'un enfant60 ou ont subi la rupture de leurs contrats commerciaux.</p>
