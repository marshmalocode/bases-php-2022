    <h2>Causes et raisons </h2>
    <p>Les raisons de l'existence de tels groupes armés composés d'enfants s'expliquent de différentes façons : </p>

<p>Outre l'endoctrinement dans les régimes totalitaires soucieux de contrôler la jeunesse et l'enlèvement,
 une des origines du phénomène des enfants soldats se trouve dans la pauvreté, aggravée par la situation de guerre. 
 Les familles ne pouvant subsister aux besoins de leurs enfants, certains se désignent pour s'enrôler dans l'armée ou 
 les groupes paramilitaires, soulageant ainsi leurs proches d'une bouche à nourrir, tout en assurant eux-mêmes leur subsistance.</p>
<p>Sur la base du volontariat, on peut également trouver d'autres motivations : </p>
<ul>
    <li>la fascination pour la vie militaire, l'uniforme, le prestige, etc. ; </li>
    <li> le désir de venger la mort d'un proche durant un conflit ou simplement de participer au combat pour la libération de son peuple ;</li>
    <li>le besoin de protection (les rangs des indépendantistes tchétchènes 
        se sont vus grossir lorsque l'armée russe a pris l'habitude d'arrêter tous les jeunes hommes de 16 ans pour les « interroger ») ; </li>
    <li>on trouve parfois des raisons idéologiques (dans ces cas-là, les enfants bénéficient souvent de l'approbation de l'entourage, 
        fier de voir un membre de la famille se sacrifier pour la cause). </li>
</ul>
<p>
<p>Du côté des recruteurs, les raisons sont facilement explicables. Les enfants ne coûtent pas cher en nourriture,
     ils sont dociles, influençables, et facilement enrôlables. De plus, ils peuvent s'avérer d'une redoutable efficacité 
     sur des terrains difficiles (la brousse par exemple), mais également comme espions ou kamikazes, passant facilement pour
      inoffensifs aux yeux des opposants. Ils sont également utilisés comme « chair à canon » sur les champs de bataille, placés
       en première ligne pour faire diversion. </p>

<p>De plus, étant jeunes et démunis de moyens de subsistances, ils sont dans la quasi impossibilité de fuir. Autres atouts : 
    intimidables et malléables, on a vite fait d'en faire des machines de guerre. Ainsi, durant la guerre civile, les enfants-loups
     de la Résistance nationale du Mozambique (RENAMO) au Mozambique étaient dressés au massacre et à la torture, tâches qu'ils 
     accomplissaient avec ferveur selon leurs propres récits. De plus, la fragilité des enfants, tant psychologique que physique est un frein à la mutinerie.</p>

<p>Il ne faut pas non plus oublier les différentes raisons d'ordre contextuel qui expliquent cet accroissement d'utilisation des enfants soldats.
     Tout d'abord, l'extrême fragilité des situations politiques et économiques des pays concernés, et les tensions exacerbes qui y règnent.
      C'est ainsi qu'un enfant issu d'une famille aisée aura bien moins de risques de se faire recruter ou enrôler qu'un autre issu d'une famille défavorisée.</p>


<p>Enfant soldat de 15 ans géorgien de la Légion des volontaires français contre le bolchevisme, décembre 1941.
De plus, le manque d'accès à l'éducation est un facteur important: les enfants n'étant pas ou plus scolarisés doivent 
trouver une occupation, qui bien souvent se doit d'être lucrative. L'engagement dans l'armée est alors une alternative au travail dans le civil. </p>

<p>Une autre explication serait la présence d'un nombre élevé d'armes légères ; en 1998, on estimait à cinq cents millions le nombre d'armes 
    légères en circulation dans le monde, dont cent millions rien qu'en Afrique.  Ces armes sont facilement manipulables,
     notamment par les enfants, ce qui accroît leur possibilité de participer aux combats.</p>

<p>Certaines sociétés sont largement militarisées. Le culte de l'armée, des armes et de la guerre augmente la part de fascination des 
    plus jeunes pour ce qui a trait de près ou de loin à l'armée ou la violence, ce qui accroît la probabilité d'engagement précoce des enfants.
     Ceci est encore plus vrai pour les pays où la scolarisation n'est pas assurée, ou largement axée sur l'endoctrinement patriotique, comme
      certaines écoles religieuses, ou aussi les écoles publiques des États belliqueux (la dictature militaire de Myanmar par exemple, ou certaines 
      écoles coraniques dans lesquelles l'éducation est en réalité axée sur l'islamisme, et non sur l'islam). </p>

<p>Le statut des fillettes (mais cela peut également arriver aux garçons) est particulier en raison de leur exploitation sexuelle. </p>

<p>Tous ces facteurs font que lors d'un conflit armé, les enfants sont, avec les femmes, des cibles faciles.</p>
