    <h2>La Convention Internationale des Droits de L'Enfant (CIDE) </h2>
     <p><strong>Parce que les enfants sont plus vulnérables que les adultes,
         parce qu’ils n’ont ni droit de vote ni influence politique ou économique, parce que le développement
          sain des enfants est crucial pour l’avenir de toute société, le monde s’est doté en 1989 de la Convention 
          internationale des droits de l’enfant (CIDE). Depuis, ce traité fondamental est le socle de toute l’action d’UNICEF.</strong><p>
            <h3>La Convention comporte 54 articles, énonçant que chaque enfant a : </h3>
            <ul><li>le droit d’avoir un nom, une nationalité, une identité </li>
            <li>le droit d’être soigné, protégé des maladies, d’avoir une alimentation suffisante et équilibrée </li>
            <li>le droit d’aller à l’école </li>
            <li>le droit d’être protégé de la violence, de la maltraitance et de toute forme d’abus et d’exploitation</li>
            <li>le droit d’être protégé contre toutes les formes de discrimination </li>
            <li>le droit de ne pas faire la guerre, ni la subir</li>
            <li>le droit d’avoir un refuge, d’être secouru, et d’avoir des conditions de vie décentes</li>
            <li>le droit de jouer et d’avoir des loisirs</li>
            <li>le droit à la liberté d’information, d’expression et de participation</li>
            <li>le droit d’avoir une famille, d’être entouré et aimé</li> </ul>

            <p><strong>La convention met en avant quatre principes fondamentaux concernant les enfants :</strong> la non-discrimination, 
                l’intérêt supérieur de l’enfant, le droit de vivre, survivre et se développer ainsi que le respect des opinions 
                de l’enfant. Trois protocoles facultatifs ont été ajoutés au texte principal. <strong>Le premier </strong> vise à protéger les enfants
                 contre le recrutement dans les conflits armés,<strong> le deuxième </strong>concerne la vente d’enfants (à des fins de travail forcé,
                  adoption illégale, don d’organes…), la prostitution ainsi que la pornographie mettant en scène des enfants.<strong> Le troisième </strong> 
                  définit la procédure internationale qui permet à tout enfant de déposer une plainte pour violation de ses droits,
                   directement auprès du Comité des droits de l’enfant des Nations unies, lorsque tous les recours ont été épuisés au niveau national.</p>


     <h3>Enfants déracinés </h3>
     <hr>
     <p><strong>Chaque enfant, sans distinction, a des droits (article 2)</strong>. Qu’il soit réfugié ou migrant,
         un enfant a droit à une protection et à des soins spécifiques jusqu’à ses 18 ans, comme n’importe quel enfant.
          Il doit pouvoir bénéficier d’alternatives sûres et concrètes à la détention. UNICEF plaide auprès des gouvernements
           pour protéger tous les enfants en situation de migration de la xénophobie et des discriminations. Nous travaillons 
           pour leur garantir un accès égal à la santé et à l’éducation, pour qu’ils soient respectés, qu’ils aient droit à une 
           famille et à une identité juridique et pour les protéger de toute forme de violence et d’exploitation.</p>
    <h3>Une chance de vivre </h3>
    <hr>
    <p>Chaque enfant a un droit inhérent à la vie, à la survie et au développement (article 6), il doit pouvoir jouir 
        du meilleur état de santé possible (article 24). Bien que des progrès remarquables aient été réalisés ces trois
         dernières décennies (diminution du nombre d’enfants décédés avant l’âge de 5 ans passant de 12,5 millions en 1990
          à 5,2 millions en 2019), ce n’est pas encore suffisant. Rien qu’en 2019, environ 14000 décès d’enfants de moins de
           cinq ans sont survenus quotidiennement et si les tendances actuelles persistent, entre 2020 et 2030, 48 millions d’enfants,
            dont la moitié des nouveau-nés, mourront avant l’âge de cinq ans. Parmi eux, beaucoup à cause de maladies qui peuvent être
             évitées ou traitées, s’ils bénéficient de soins abordables et de qualité. UNICEF plaide pour une couverture sanitaire
              universelle pour que chaque enfant survive et s’épanouisse.</p>

<p class="blue"><strong>La vaccination </strong> participe de ces droits. Grâce aux efforts considérables entrepris ces dernières années, 
85% des enfants ont été protégés grâce à des vaccins vitaux en 2019. Toutefois, on constate un recul alarmant du nombre 
d’enfants bénéficiant de vaccins vitaux apparait dans le monde. Résultat des perturbations provoquées par la pandémie de
 COVID-19 dans la fourniture et l’utilisation des services de vaccination, ce phénomène représente un risque mortel pour les enfants qui en paient le prix.</p>

 <p><strong>La nutrition </strong>joue elle aussi un rôle majeur : en dépit des avancées, la moitié des morts d’enfants de moins de cinq ans est liée
 à la sous-nutrition. Chez les enfants, la sous-nutrition augmente les risques de mourir d’infections courantes. Celles-ci surviennent 
 plus fréquemment et sont souvent plus graves. Des progrès ont été observés : entre 1990 et 2018, le nombre d’enfants de moins de cinq
  ans souffrant d’un retard de croissance a diminué dans le monde passant de 253 millions à 144 millions.  Dans un contexte de crise 
  sanitaire telle que celle liée au Covid-19, la nutrition est plus importante que jamais : elle aide à renforcer les défenses immunitaires
   contre toutes sortes de maladies. Les programmes d’actions pour faire face à la malnutrition sont plus que jamais indispensables. </p>
 
 <p class="blue"><strong>  
L’accès à une eau saine et à l’hygiène </strong> est tout aussi primordial. Bien que le lavage des mains avec du savon soit essentiel dans 
la lutte contre les maladies infectieuses, y compris la COVID-19, 40 % de la population mondiale, soit 3 milliards de personnes,
 n’ont pas d’installation pour se laver les mains avec de l’eau et du savon à la maison.  En 2017, 2,2 milliards de personnes ne 
 disposaient pas d’accès à l’eau potable et 4,2 milliards n’étaient pas en mesure d’utiliser des installations sanitaires sûres. 
 Pour vivre, survivre et se développer, il faut boire une eau potable, faire ses besoins dans des toilettes de base et bénéficier
  de bonnes conditions d’hygiène. Nous demandons la mise en place durable de services d’approvisionnement en eau et de systèmes d’assainissement.</p>
    
  <h3>Mettre fin à la violence envers les enfants </h3>
  <hr>
  <p><strong>Chaque enfant a le droit d’être protégé contre toute forme de violence physique ou mentale (article 19). 
    Il a aussi le droit d’être entendu (article 12).</strong> C’est la raison pour laquelle UNICEF accorde autant d’importance à la prise
     de parole des enfants et des jeunes. Plus d’un million d’enfants nous ont indiqué ce qui les ferait se sentir en sécurité à l’école.
      Cela a donné naissance au <a href="https://www.unicef.org/fr/mettre-fin-violence/manifeste-de-la-jeunesse-pour-mettre-fin-%C3%A0-la-violence" target="_blank"> 
        Manifeste de la jeunesse pour mettre fin à la violence. </a>Celui-ci est une tribune adressée aux élèves,
       aux parents, aux professeurs et aux gouvernements pour améliorer la sécurité dans les écoles.</p>

<p>La CIDE a poussé les États à modifier leur législation pour que les enfants soient reconnus comme les détenteurs 
    individuels de droits. Dans 54 pays, il est désormais interdit de faire usage de châtiments corporels, y compris sein du foyer,
     et 56 autres États se sont engagés à faire évoluer leur législation en ce sens.</p>

<p>Par ailleurs,<strong> chaque enfant a le droit d’être protégé contre toute forme d’exploitation et de violence sexuelle (article 34).</strong>
     Or, 650 millions de filles et de femmes actuellement en vie ont été mariées avant leurs 18 ans. Lorsqu’un mariage a lieu avant
      la majorité cela entraine plus de risques de violence domestique et de déscolarisation. Au cours des dix dernières années,
       la proportion de femmes mariées alors qu’elles étaient encore mineures a diminué de 15%, mais l’impact de la pandémie de la
        COVID 19 menace ces progrès : 13 millions de mariages d’enfants sont prédits dans les 10 prochaines années s’ajoutant aux 
        150 millions de cas déjà prévus pour cette même période, 4 millions de filles pourraient être mariées avant leur 18 ans dans les deux prochaines années.</p>

<h3>Les premiers moments comptent</h3>
<hr>
<p><strong>Chaque enfant a un droit inhérent à la vie, à la survie et au développement (article 6). </strong>
    Pour que ce droit soit pleinement respecté, les parents doivent élever leurs enfants avec affection, au moyen d’une bonne nutrition,
     de jeux et de soins. C’est primordial dans les premières étapes de la vie.</p>
<p><strong>
La CIDE oblige les États à faire en sorte que les enfants dont les parents travaillent ne soient pas privés de ce dont ils ont besoin (article 18).</strong>
 Il convient de souligner que de bonnes conditions de travail permettent aux parents de subvenir aux besoins de leur enfant. Pour UNICEF, 
 il est indispensable que les gouvernements et les entreprises créent des environnements de travail favorables aux familles, pour 
 que les enfants bénéficient du meilleur départ possible dans la vie.</p>

 <h3>Éducation </h3>
<hr>
<p><strong>Chaque enfant a droit à l’éducation et à des possibilités d’apprentissage de qualité (article 28).</strong> Plus d’enfants et de jeunes 
sont inscrits à l’école. Pourtant, aujourd’hui le monde connait une véritable crise de l’apprentissage : 250 millions d’enfants et d’adolescents 
en âge d’aller à l’école primaire ou au collège ne sont pas scolarisés – ils étaient 364 millions en 1989). La fermeture des écoles liée à la crise 
sanitaire de la COVID-19 a renforcé les réalités déjà difficiles pour les enfants et les jeunes.  Au moins un tiers des élèves – soit 463 millions
 d’enfants dans le monde – n’ont pas eu accès à l’enseignement à distance lorsque leur école était fermée. </p>

 <p>Avec nos partenaires, nous mettons en place des systèmes éducatifs pour offrir un apprentissage à tous les enfants, 
    pour que chacun dispose des connaissances et des compétences nécessaires pour s’épanouir à l’âge adulte.</p>

<p>La quasi-totalité des pays signataires de la convention des droits de l’enfant ont rendu obligatoire la scolarisation primaire,
     et plus de 100 d’entre eux offrent au moins neuf années d’enseignement obligatoire et gratuit.</p>
        
