<h2>Histoire</h2>

<h3> En France </h2>
<hr>
<p>Au Moyen Âge, en France, l'entraînement du chevalier commençait vers 9~10 ans (parfois dès sept ans),
 avec un entraînement militaire. Vers 12~14 ans, devenu écuyer, il suivait un chevalier et se confrontait
à la férocité des combats. Vers 18~20 ans, il était adoubé et devenait à son tour un chevalier </p>

<p>La période révolutionnaire, en France, continue la pratique de l'ancien régime de l'engagement de jeunes
 garçons comme tambours dans les armées dès l'âge de treize ans. Pour des raisons de propagande idéologique,
elle organise un « culte républicain » à certaines de ces recrues qui sont morts au combat comme Joseph Barra,
 Agricol Viala ou Pierre Bayle.</p>

 <h3>En Allemagne </h3>
<hr>
<p>Carl von Clausewitz participe au siège de Mayence (1793) à l'âge de treize ans en tant que porte-enseigne. 
    Quelques siècles plus tard, lors de la bataille de Berlin, le jeune Alfred Czech sera le plus jeune soldat
     décoré par Adolf Hitler et représentera, dans l'imaginaire collectif, le sacrifice fanatique des Jeunesses
      hitlériennes lors des dernières semaines du Troisième Reich. </p>

<h3>Aux Etats-Unis </h3>
<hr>
<p>Au cours des XIXe et XXe siècles, des enfants soldats sont utilisés durant la guerre de Sécession, aux États-Unis dans les années 1860. </p>

<h3>En France (XXe siècle)</h3>
<hr>
<p>Lors de la déclaration de la Première Guerre mondiale beaucoup d'enfants ont tenté de s'engager, ce que le Breton Jean-Corentin Carré
     réussit à faire en mentant sur son âge, devenant à l'époque un « symbole de courage et de patriotisme » </p>

<h3>Seconde Guerre mondiale </h3>
<hr>
<p>Lors de l'insurrection de Varsovie en Pologne durant la Seconde Guerre mondiale, des enfants ont participé comme 
    combattants ou soignants. Une statue de bronze, Le Petit Insurgé, commémorant la participation des enfants soldats, a été installée à Varsovie6.
   À la fin de la guerre, les Jeunesses hitlériennes ont été utilisées, elles ont ralenti la progression des Alliés dans des combats
   désespérés, notamment face à l'Armée rouge lors de la chute de Berlin.
     L'Armée rouge a, elle aussi, eu ses contingents d'enfants soldats.
</p>
<p> Le film d'Andreï Tarkovski,L'Enfance d'Ivan (1962), tiré du récit de Vladimir Bogomolov (1926-2003), témoigne du destin souvent tragique de ces enfants :</p>
<p>Âgés de 16-17 ans, ils étaient entre 60 000 et 300 000, et 59 % d'entre eux étaient russes et d'origine paysanne.
     Cet engagement s'inscrivait dans le 
     système d'endoctrinement par l'État soviétique de la génération de l'entre-deux-guerres
     et s'explique par des sentiments relevant de la bravade, de la vantardise mêlée d’exaltation face au danger, typiques de 
     l’adolescence, ainsi que par des pressions du milieu social</p>
</p>
<p>Les survivants ont été décorés et adulés par la population russe :</p>
   <p>« À la fin de la Seconde Guerre mondiale, après une période de réticence à parler de la présence des enfants dans les
             opérations militaires, leur participation a été reconnue et célébrée, s'est même inscrite dans « la mémoire du sang 
             versé » et a fait l'objet d'un véritable culte, le culte des Pionniers victimes des opérations de guerre sur laquelle 
             le régime a construit la relance morale et idéologique des organisations de jeunesse, notamment celle des Pionniers. » </p>
<h3>Années 2000-2010 </h3>
<hr>
<p>L'Organisation des Nations unies (ONU) estime à 300 000 le nombre d'enfants soldats en activité, au début des années 20108.
     Ils sont principalement utilisés lors des guerres civiles, ou par des groupes criminels paramilitaires en Colombie ou des mouvements
      de guérilla révolutionnaires (comme les FARC en Colombie)[réf. nécessaire]. Ainsi, l'UNICEF estime à plus de 5 000 le nombre d'enfants 
      soldats recrutés par les LTTE[réf. nécessaire]. Au Nigeria, Boko Haram fait un usage répété d'enfants comme kamikazes avec 83 cas durant les huit premiers mois de 2017 </p>
   <p>  Au Mexique, l'EZLN a été dénoncée en 1994 par l'ONG Human Rights Watch pour avoir enrôlé des enfants de six à quinze ans10.</p>
<p>Les talibans présents en Afghanistan ont également procédé à l'enrôlement de jeunes guerriers à partir de leur entrée en guerre contre les moudjahidines en 1994 jusqu'à la chute du régime islamiste en 2001.</p>
  <p>  L'utilisation d'enfants soldats n'a pas disparu au sein des groupes talibans opérant après la chute de leur régime et l'on estime en 2001 la proportion d'enfants soldats 
    en Afghanistan à 20 % de l'effectif total11. Selon l'Organisation des Nations unies, la Police nationale afghane intègre aussi des enfants dans ses rangs. Un accord est signé au début de l'année 2010 pour mettre fin à cette pratique. </p>

<h3>Ouganda</h3>
<hr>
<p>L'Armée de résistance du Seigneur (LRA), un groupuscule de fanatiques religieux du Nord de l'Ouganda dirigé par Joseph Kony procède
     régulièrement, et ce depuis le milieu des années 1990, à des enrôlements forcés de jeunes enfants. Un rapport de la BBC datant de 
     2000 avance le chiffre de 20 000 enfants kidnappés, parfois dès l'âge de sept ou huit ans. Ces rapts sont souvent accompagnés de 
     violences faites à la famille de l'enfant, parfois exécutées sous les yeux des enfants enlevés. De plus, les « petits soldats » sont
      forcés de participer à des exactions, constituant des crimes de guerre (massacres de villages entiers, exécutions sommaires, lapidations,
       etc.). Ceux qui tentent de fuir servent d'exemple en étant exécutés par des meurtres rituels auxquels participent tour à tour les enfants
        du contingent. Les membres de la LRA forcent parfois les enfants à commettre ces exactions dans leur propre village, les dissuadant une
         fois pour toutes de tenter de rentrer chez eux un jour.</p>
<p>Les enfants enlevés subissent eux aussi des actes de barbarie (viols, meurtres, tortures, travaux forcés, longues marches, etc.).
     La proportion d'enfants dans le contingent total de la LRA est estimée à 80 %, c'est donc une armée vivant principalement de ces rapts.</p>

<h3>République démocratique du Congo </h3>
<hr>
<p>Les différentes milices ont largement employé des enfants comme combattants lors de la Deuxième guerre du Congo. Certains groupes armés avaient jusqu’à 40 % d’enfants dans leurs troupes.

Pour mettre fin à la guerre civile, un programme de désarmement, de démobilisation et de réinsertion (DDR) des anciens 
combattants dans la vie civile a été initié en 2003. Il concernait 300 000 enfants. Mais ce programme a connu plusieurs freins qui 
ont fait qu'en juin 2006, la commission gouvernementale chargée du programme DDR a annoncé qu'elle avait démobilisé un peu plus de 
19 000 enfants, dans un rapport publié en octobre 2006, considère que ce programme n'apporte pas aux enfants la protection et le soutien dont ils ont besoin</p>

<p>En raison des difficultés pour survivre et de la persistance de groupes armés, de nombreux enfants risquent de rejoindre de nouveau des milice. </p>
<p>Les filles, qui représentaient 40 % des enfants soldats ont peu profité du programme DDR. Selon Amnesty International, 
 les filles ont souvent été « mariées » de force à des soldats adultes et des responsables refusent de les reconnaître comme
  enfants soldats préférant les désigner comme « personnes à charge » de combattants adultes, ou de soldats péripatéticiens </p>
  <p>La Mission de l'Organisation des Nations Unies en République démocratique du Congo (MONUC) condamne, dans un communiqué 
du 14 décembre 2007, la présence de centaines d’enfants soldats, filles et garçons, dans la guerre du Kivu. Ces enfants 
 sont recrutés de force dans les écoles par les groupes armés, principalement par le Congrès national pour la défense du peuple (CNDP) 
  et les Forces démocratiques de libération du Rwanda (FDLR), depuis la reprise des combats en août 2007. Les enfants deviennent
 des combattants ou sont utilisés pour des tâches logistiques ou comme esclaves sexuels </p>

 <h3>Syrie </h3>
 <hr>
 <p>Selon Human Rights Watch, des enfants syriens serviraient dans l'Armée syrienne libre19,20. 
    Plusieurs autres organisations ont prouvé la présence d'enfants combattants pour l'armée rebelle dans le conflit syrien.
     Certains groupes islamistes comme l'EI recruteraient de jeunes adolescents pour des attaques-suicides. </p>
     <p> Selon le site américain Slate, l'EI forme des enfants d'à peine six ans à des fins militaires. L'organisation 
        a mis sur pied un système qui permet de recruter et d'endoctriner les enfants. Les enfants sont soit enlevés,
         soit rejoignent « consciemment » l'organisation. Ils rejoignent après des camps d’entraînement où ils sont endoctrinés et sont formés aux maniement des armes. 
        Les enfants servent ensuite de fantassins, de « mouchards », de kamikazes, et leur sang est utilisé pour des transfusions. Selon, l'ONU, l'EI utilise des enfants dans les attentats-suicide </p>




