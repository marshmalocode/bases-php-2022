    <h3>QUI SONT LES ENFANTS-SOLDATS ? </h3> 
   <p> Les Nations Unies (ONU) ont élaboré une définition de l’enfant-soldat pour que tous les pays ayant ratifié la
     Convention des droits de l’enfant (CIDE) de 1989 partagent la même référence.
L’article 1 de la Convention stipule : "Un enfant-soldat s’entend de tout être humain âgé de moins de 18 ans,
 sauf si la majorité est atteinte plus tôt en vertu de la législation qui lui est applicable".</p>

<p>Un enfant-soldat a donc moins de 18 ans. Il peut être très jeune : certains enfants sont recrutés dès leur plus jeune âge,
     dès qu’ils peuvent se rendre utiles (par exemple en portant du matériel, en transmettant des messages...)</p>

<p>Tu as sans doute vu de nombreuses photos de jeunes garçons portant des armes, mais il faut savoir que les filles sont
     toutes aussi concernées par le phénomène. Elles représentent en fait la moitié des enfants recrutés.</p>
    <p> Un enfant soldat est un enfant combattant, fille ou garçon, âgé de moins de dix-huit ans, et parfois dès six ans ;
    selon le protocole facultatif à la Convention internationale des droits de l'enfant, concernant l'implication d'enfants dans les conflits armés</p>
    <p>L'enrôlement d'enfants de moins de quinze ans dans des forces armées est un crime de guerre.</p>
    <p> Les adolescents de quinze à dix-sept ans ressortissent d'un statut intermédiaire mal défini.
      Le terme est également utilisé lors de conflits armés à faible impact au niveau politique, notamment concernant des affrontements entre bande rivales.
     </p>

    <h3> QUE FONT LES ENFANTS-SOLDATS ? </h3>

<p>
Les enfants-soldats ne sont pas tous... des soldats ! Il n’est pas nécessaire qu’ils portent des armes pour entrer dans la définition. 
L’important est qu’ils soient enrôlés, de gré ou de force, dans un groupe armé étatique ou rebelle, et ce, quelle que soit la fonction qu’ils exercent. 
Ils peuvent donc tout aussi bien être des cuisiniers, porteurs, messagers, esclaves sexuel(lles)...</p>
<p>
Les enfants-kamikazes sont également de plus en plus nombreux dans de nombreux pays. Des gamins d’à peine six ans sont "formés" dans un but :
     mourir pour la cause. Une chose est sûre : il ignorent souvent leur rôle dans les opérations meurtrières qui leur sont imposées.</p>

