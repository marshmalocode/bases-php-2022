        <h1>L'autisme</h1>
        <h2>Définition</h2>
        <p>Grave trouble du développement qui réduit la capacité à communiquer et à interagir.
Les troubles du spectre de l'autisme ont une incidence sur le système nerveux.
L'étendue et la gravité des symptômes sont très variables. Les symptômes courants possibles sont des difficultés à communiquer et à appréhender les interactions sociales, des intérêts obsessionnels, et des comportements répétitifs.
Une détection précoce, ainsi que des thérapies comportementales, éducatives et familiales peuvent réduire les symptômes et favoriser le développement et l'apprentissage.
</p>
        <h2>Symptômes</h2>
        <p>L'étendue et la gravité des symptômes sont très variables. Les symptômes courants possibles sont des difficultés à communiquer et à appréhender les interactions sociales, des intérêts obsessionnels, et des comportements répétitifs.<br>

Les personnes atteintes peuvent présenter les symptômes suivants :<br>
Comportementaux: contacts visuels insuffisants, interactions sociales inappropriées, automutilation, comportement compulsif, impulsivité, mouvements répétitifs ou répétition persistante de mots ou d'actions
Développement: retard de la parole chez l'enfant ou troubles de l'apprentissage
Cognitifs: intérêt intense pour un nombre limité de choses ou problème de concentration
Psychologiques: n'a pas conscience des émotions d'autrui ou dépression
Autres symptômes courants: anxiété, changement de voix, sensibilité au son ou tic
</p>
