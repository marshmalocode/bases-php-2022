        <h1>La démence</h1>
        <h2>Définition</h2>
        <p>Groupe de symptômes en relation avec la réflexion et la sociabilité, qui interfèrent avec la vie quotidienne.
La démence n'est pas une maladie spécifique, mais un groupe de pathologies caractérisées par l'affaiblissement d'au moins deux fonctions cérébrales, comme la mémoire et le jugement.
Les symptômes sont des problèmes de mémoire, un manque de sociabilité et des capacités de réflexion si altérées que cela interfère avec la vie quotidienne.
Des médicaments et des thérapies peuvent aider à gérer les symptômes. Certaines causes sont réversibles.</p>

        <h2>Symptômes</h2>
        <p>
Les symptômes sont des problèmes de mémoire, un manque de sociabilité et des capacités de réflexion si altérées que cela interfère avec la vie quotidienne.

Les personnes atteintes peuvent présenter les symptômes suivants :<br>
Cognitifs: déclin mental, confusion en fin de journée, confusion mentale, désorientation, incapacité à parler ou à comprendre des mots, incapacité à reconnaître des objets courants ou élucubrations.<br>
Comportementaux: fébrilité, irritabilité, errance et égarement, manque de retenue ou modifications de la personnalité
Humeur: nervosité, anxiété, sautes d'humeur ou solitude
Psychologiques: hallucinations, dépression ou paranoïa
Musculaires: incapacité à combiner les mouvements musculaires ou marche instable
Autres symptômes courants: perte de mémoire, chute, paroles indistinctes ou troubles du sommeil
Le traitement dépend du stade d'avancement.
Des médicaments et des thérapies peuvent aider à gérer les symptômes. Certaines causes sont réversibles.
</p>
