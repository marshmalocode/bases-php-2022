        <h1>Les troubles de l'anxiétés</h1>
        <h2>Définition</h2>
        <p>Trouble mental caractérisé par des sentiments d'inquiétude, d'anxiété ou de crainte qui sont suffisamment prononcés pour perturber les activités quotidiennes de la personne.
Au nombre des troubles anxieux figurent les crises de panique, les troubles obsessionnels compulsifs et le trouble de stress post-traumatique.
Les symptômes possibles sont un stress disproportionné par rapport à l'impact de l'événement, une incapacité à mettre de côté un souci et une agitation.
Le traitement consiste à suivre une psychothérapie ou à prendre des médicaments, tels que des antidépresseurs.
</p>
        <h2>Symptômes</h2>
        <p>Les symptômes possibles sont un stress disproportionné par rapport à l'impact de l'événement, une incapacité à mettre de côté un souci et une agitation.<br>

Les personnes atteintes peuvent présenter les symptômes suivants :<br>
Comportementaux: fébrilité, irritabilité ou hypervigilance
Cognitifs: manque de concentration, pensées indésirables ou pensées qui défilent
Corps entier: fatigue ou transpiration
Autres symptômes courants: anxiété, inquiétude excessive, insomnie, nausées, palpitations, peur, sensation de danger imminent ou tremblement
</p>
