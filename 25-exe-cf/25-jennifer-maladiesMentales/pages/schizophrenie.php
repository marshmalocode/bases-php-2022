<h1>La schizophrénie</h1>
<h2>Définition</h2>
<p>Trouble qui affecte la capacité d'une personne à réfléchir, à ressentir et à se comporter de manière claire.
Les causes exactes de la schizophrénie restent inconnues à ce jour, mais une combinaison de facteurs génétiques et environnementaux, et d'une altération de la structure et de la chimie du cerveau pourrait jouer un rôle.
La schizophrénie se caractérise par des pensées ou des expériences qui semblent complètement détachées de la réalité, un discours ou un comportement désorganisé, et une participation réduite aux activités de la vie quotidienne. Des difficultés de concentration et des troubles de la mémoire sont également possibles.
Le traitement est généralement à vie, et implique une combinaison de médicaments, de psychothérapie et de services de soins de spécialité coordonnés.
</p>
<h2>Symptômes</h2>
<p>La schizophrénie se caractérise par des pensées ou des expériences qui semblent complètement détachées de la réalité, un discours ou un comportement désorganisé, et une participation réduite aux activités de la vie quotidienne. Des difficultés de concentration et des troubles de la mémoire sont également possibles.<br>
Les personnes atteintes peuvent présenter les symptômes suivants :<br>
Comportementaux: isolement, comportement désorganisé, aggressivité, automutilation, comportement compulsif, excitabilité, agitation, hostilité, manque de retenue ou mouvements répétitifs
Cognitifs: trouble de la pensée, délire, lenteur en cas d'activité, amnésie, confusion mentale, croyance erronée en sa supériorité, croyance qu'un événement banal possède une signification particulière ou personnelle, désorientation ou impression que des pensées étrangères s'infiltrent dans son esprit
Humeur: perte d'intérêt ou de plaisir à effectuer diverses activités, réaction émotionnelle inappropriée, anxiété, apathie, colère, détachement de soi ou mécontentement général
Psychologiques: hallucinations, impression d'entendre des voix, paranoïa, délire de persécution, délire religieux, dépression ou peur
Voix: troubles du langage, discours circonstanciel, discours incohérent ou débit de parole rapide et frénétique
Autres symptômes courants: fatigue, manque de réaction émotionnelle, perte de mémoire ou trouble de la planification motrice.
</p>
