        <h1>Les troubles obsétionelle compulsif</h1>
        <h2>Définition</h2>
        <p>
        Pensées intrusives (obsessions) qui conduisent à un comportement répétitif (compulsions).
Les troubles obsessionnels compulsifs se caractérisent par des pensées et des peurs irraisonnées (obsessions) qui conduisent à un comportement compulsif.
Les TOC tournent fréquemment autour de thèmes tels que la phobie des germes ou le besoin de ranger des objets d'une manière bien précise. Les symptômes apparaissent graduellement et varient tout au long de la vie.
Le traitement comprend des séances de psychothérapie, la prise de médicaments, voire les deux.


        </p>
        <h2>Symptômes</h2>
        <p>
Les TOC tournent fréquemment autour de thèmes tels que la phobie des germes ou le besoin de ranger des objets d'une manière bien précise. Les symptômes apparaissent graduellement et varient tout au long de la vie.<br>

Les personnes atteintes peuvent présenter les symptômes suivants :<br>
Comportementaux: comportement compulsif, comportement ritualiste, isolement, accumulation compulsive, agitation, hypervigilance, impulsivité, mouvements répétitifs, répétition de mots dénués de sens ou répétition persistante de mots ou d'actions
Humeur: culpabilité, anxiété, appréhension ou crises de panique
Psychologiques: dépression ou peur
Autres symptômes courants: aversions alimentaires, cauchemars ou ressassement des pensées
