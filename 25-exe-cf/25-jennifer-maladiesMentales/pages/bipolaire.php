        <h1>Les troubles bipolaires</h1>
        <h2>Définition</h2>
        <p>Trouble de l'humeur caractérisé par des épisodes dépressifs et maniaques.
Les causes exactes du trouble bipolaire restent inconnues à ce jour, mais une combinaison de facteurs génétiques et environnementaux, et d'une altération de la structure et de la chimie du cerveau pourraient jouer un rôle.
Les symptômes possibles des épisodes maniaques sont un débordement d'énergie, une perte de sommeil ainsi qu'une perte de contact avec la réalité. Les épisodes dépressifs, quant à eux, se caractérisent notamment par les symptômes suivants : manque d'énergie, faible motivation et désintérêt des activités quotidiennes. Ces épisodes durent de quelques jours à quelques mois et peuvent s'accompagner de pensées suicidaires.
Le traitement, généralement à vie, allie souvent médicaments et psychothérapie.
</p>
        <h2>Symptômes</h2>
        <p>Les symptômes possibles des épisodes maniaques sont un débordement d'énergie, une perte de sommeil ainsi qu'une perte de contact avec la réalité. Les épisodes dépressifs, quant à eux, se caractérisent notamment par les symptômes suivants : manque d'énergie, faible motivation et désintérêt des activités quotidiennes. Ces épisodes durent de quelques jours à quelques mois et peuvent s'accompagner de pensées suicidaires.<br>

Les personnes atteintes peuvent présenter les symptômes suivants :<br>
Humeur: tristesse, sautes d'humeur, culpabilité, désespoir, euphorie, perte d'intérêt ou de plaisir à effectuer diverses activités, anxiété, apathie, appréhension, colère, mécontentement général ou perte d'intérêt
Comportementaux: irritabilité, comportement désorganisé, comportement à risque, aggressivité, automutilation, fébrilité, agitation, excès de désir sexuel, hyperactivité, impulsivité ou pleurs
Cognitifs: pensées indésirables, délire, lenteur en cas d'activité, croyance erronée en sa supériorité, manque de concentration ou pensées qui défilent
Psychologiques: dépression, épisode maniaque, dépression avec agitation ou paranoïa
Poids: perte de poids ou prise de poids
Sommeil: difficultés d'endormissement ou somnolence excessive
Autres symptômes courants: débit de parole rapide et frénétique ou fatigue
</p>
