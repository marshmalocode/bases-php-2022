        <h1>Le troubles de l'attention et hyperactivités</h1>
        <h2>Définition</h2>
        <p>
        Maladie chronique caractérisée par des troubles de l'attention, une hyperactivité et une impulsivité.
Le trouble du déficit de l'attention avec ou sans hyperactivité (TDAH) apparaît souvent à l'enfance et peut se poursuivre à l'âge adulte. Il peut être caractérisé par une faible estime de soi, des relations difficiles et des difficultés à l'école ou au travail.
Les symptômes sont une attention limitée et de l'hyperactivité.
Les traitements incluent la prise de médicaments et la psychothérapie.
        </p>
        <h2>Symptômes</h2>
        <p>Les symptômes sont une attention limitée et de l'hyperactivité.
<br>
Les personnes atteintes peuvent présenter les symptômes suivants :<br>
Comportementaux: aggressivité, excitabilité, irritabilité, gigotement, hyperactivité, impulsivité, manque de retenue ou répétition persistante de mots ou d'actions
Cognitifs: capacité d'attention limitée, difficulté à rester concentré, distraction, problème de concentration ou étourderie
Humeur: ennui, excitation, anxiété, colère ou sautes d'humeur
Autres symptômes courants: dépression ou troubles de l'apprentissage
</p>
