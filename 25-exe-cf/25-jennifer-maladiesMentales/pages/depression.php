        <h1>La dépréssion</h1>
        <h2>Définition</h2>
        <p>Trouble mental caractérisé par un état dépressif persistant ou une perte d'intérêt pour tout type d'activité, ce qui produit des effets très néfastes dans la vie quotidienne.
Ce trouble peut être causé par une détresse qui peut être à la fois biologique, psychologique et sociale. De plus en plus, les études montrent que ces facteurs peuvent entraîner des modifications dans le fonctionnement du cerveau, y compris un changement dans l'activité de certains circuits neuronaux.
Le sentiment persistant de tristesse ou la perte d'intérêt qui caractérise une dépression majeure peut provoquer une série de symptômes physiques et comportementaux. Ceux-ci peuvent être des troubles du sommeil, une perte d'appétit, une baisse d'énergie, une concentration altérée, un changement de comportement au quotidien ou une faible estime de soi. La dépression peut également être associée à des pensées suicidaires.
La base du traitement est généralement la prise de médicaments et/ou des séances de psychothérapie. De plus en plus, les études montrent que ces traitements peuvent réguler les modifications cérébrales associées à la dépression.
</p>
        <h2>Symptômes</h2>
        <p>Le sentiment persistant de tristesse ou la perte d'intérêt qui caractérise une dépression majeure peut provoquer une série de symptômes physiques et comportementaux. Ceux-ci peuvent être des troubles du sommeil, une perte d'appétit, une baisse d'énergie, une concentration altérée, un changement de comportement au quotidien ou une faible estime de soi. La dépression peut également être associée à des pensées suicidaires.<br>
Humeur : culpabilité, désespoir, perte d'intérêt ou de plaisir à effectuer diverses activités, tristesse, anxiété, apathie, mécontentement général, perte d'intérêt ou sautes d'humeur
Comportementaux : fébrilité, irritabilité, isolement, agitation ou pleurs excessifs
Sommeil : réveil précoce, sommeil agité, insomnie ou somnolence excessive
Corps entier : fatigue, faim excessive ou perte d'appétit
Cognitifs : lenteur en cas d'activité ou manque de concentration
Poids : perte de poids ou prise de poids
Autres symptômes courants : pensées suicidaires, petit appétit ou ressassement des pensées
</p>
