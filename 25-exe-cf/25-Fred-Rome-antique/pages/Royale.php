    <p class="txtrome2">La Royauté romaine (en latin : Regnum Romanum) est la première et la plus méconnue des périodes de l'histoire de la Rome antique. Selon la tradition, la royauté commence avec la fondation de Rome par Romulus et Rémus en l'an 753 av. J.-C., et se termine à la révolution de 509 av. J.-C. qui chasse le dernier roi, Tarquin le Superbe, pour instaurer la République romaine. Les historiens romains identifient sept rois : Romulus (avec selon certaines sources Titus Tatius), Numa Pompilius, Tullus Hostilius, Ancus Marcius, considérés comme plus ou moins légendaires, puis des rois d'origine étrusque : Tarquin l'Ancien, Servius Tullius et Tarquin le Superbe. <br> <br>

    Les récits des auteurs antiques rédigés plusieurs siècles après la période de la royauté sont plus ou moins largement mis en doute par la critique historique moderne, qui en souligne la faible fiabilité en raison d'éléments légendaires ou anachroniques, et de son caractère orienté, idéalisant les premiers rois pour noircir le dernier Tarquin. <br>

    À l'issue de cette période, Rome apparaît, lorsque l'archéologie complète la tradition, comme une cité-État organisée, avec ses temples et son urbanisation, la principale ville du Latium ouverte aux influences grecques et étrusques. <br>

    En toutes circonstances, il convient d'éviter l'expression "monarchie romaine" qui crée une confusion avec le régime impérial. 
