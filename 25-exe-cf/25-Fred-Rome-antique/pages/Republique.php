    <p class="txtrome3">Le mot « république » vient du (latin : res publica), ce qui signifie « la chose publique ». Gouverner la cité est donc une affaire publique et collective. La devise de la République est Senatus populusque romanus, « le Sénat et le peuple romain ». Elle symbolise l'union du Sénat romain, où siègent à l'origine les familles patriciennes, et de l'ensemble des citoyens romains. En effet, les Romains sont divisés en deux groupes, les patriciens et les plébéiens. Ces derniers forment la masse des artisans et paysans. Ils vivent en dehors de l'organisation patricienne et n'honorent aucun ancêtre particulier. Les patriciens sont souvent propriétaires de vastes domaines cultivés. Ils appartiennent à de célèbres familles, les gentes. Chaque gens a ses propres cultes dont celui des ancêtres et ses traditions. Elle comprend un nombre plus ou moins grand de clients qui doivent obéissance à leur « patron » et reçoivent en échange aide et assistance en cas de besoin.</p>
    
