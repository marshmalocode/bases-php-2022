            <h2>Diversité culturelle</h2>
            <p>Différents groupes socioculturels sont représentés au sein de la population camerounaise. À l'image de
                ses milieux naturels contrastés, le Cameroun est d'une grande diversité humaine. Trois grands ensembles
                peuvent être identifiés:

                Au grand nord, on distingue principalement deux grands groupes. Les Peuls (ou foulbés) et les « Kirdis
                ». Parmi ces « Kirdis », les montagnes du Cameroun depuis la région de Garoua jusqu'à Mora abritent une
                grande variété d'ethnies non-musulmanes. On y trouve généralement les ethnies Mofu, Mafa, Toupouri,
                Moundang, Guiziga, Massa, etc. Aussi, les Peuls des savanes du Nord se sont souvent organisés en
                Lamidats dirigés par un lamido, l'équivalent d'un chef de village. Leurs constructions sont encore
                visibles à ce jour et leurs coutumes perdurent. Les populations du Centre et du Sud possèdent également
                leurs coutumes, caractérisées par une très grande diversité linguistique. Les habitations des anciens
                chefs traditionnels ont presque disparu au profit de constructions modernes, la zone étant la plus
                développée du pays, mais plusieurs monuments commémoratifs y sont érigés.
                Au grand ouest, sont présents les Bamilékés (groupe dynamique dans le commerce, où ils excellent), le
                plus grand groupe ethnique du pays, aux côtés des Tikar (descendants de populations du nord) ainsi que
                des Bamouns (renommés pour leur histoire – surtout politique et militaire – et leurs créations
                artistiques). Ces groupes ont développé une civilisation originale, basée sur des chefferies qui sont
                autant de petits royaumes.

                Messager Sawa pendant la fête du Ngodo.
                Au grand sud, les principaux groupes sont les Beti (groupe principal de la zone forestière du centre,
                sud et est), les Eton, les Manguissa, les Ewondo, les Boulou, qui se rattachent au monde bantou. Les
                Bétis/Boulou, ethnie à laquelle le président Paul Biya appartient, détiennent de facto le pouvoir depuis
                198250. Les Bassa, les Yabassi, les Dibom (au centre-ouest et le littoral géographique du pays), et les
                Sawa et apparentés (peuplant la zone côtière) sont les autres principaux peuples. Les Bassa sont
                majoritairement installés dans plusieurs villes, en commençant par Éséka en passant par Édéa jusqu’à
                Yabassi et un peu dans le Moungo et le Wouri. Les Bassa sont structurés en plusieurs petits groupes. Les
                Gbaya, occupants majoritaires de plus de six unités administratives des régions de l'Est et de
                l'Adamaoua. Les Gbaya, faiblement représentés dans la classe politique, sont locuteurs de plusieurs
                dialectes : laii (Bétaré-Oya), do'oka (Garoua-Boulaï), yayoué (Meiganga) Bodomo… Les pygmées du Sud
                vivent principalement dans la forêt.</p>
