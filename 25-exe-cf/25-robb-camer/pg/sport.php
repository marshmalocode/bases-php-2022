 <h2> L'exploit du Mondial 90</h2>
 <p>Au sortir de la CAN 1988, Le Roy quitte le Cameroun pour le Sénégal, et le Russe Valeri Nepomniachi 
    (qui ne parle que quelques mots d'anglais et pas un mot de français) le remplace. En 1989 les Camerounais 
    se qualifient pour leur deuxième Coupe du monde en 1990, en écartant au tour final la Tunisie.
     En mars 1990 ils perdent leur titre continental lors de la CAN en Algérie, après deux défaites face à 
     la Zambie et au Sénégal en phase de poules</p>
 <p>Arrivés en Italie pour la Coupe du monde, les Lions indomptables causent une des plus grandes surprises 
    de l'histoire de la compétition en s'imposant lors du match d'ouverture contre le tenant du titre, 
    l'Argentine de Diego Maradona (1-0, but de François Omam-Biyik)17. Après une deuxième victoire obtenue
     contre la Roumanie (2-1, doublé de Roger Milla) et une déconvenue contre l'URSS (0-4), ils se qualifient pour
      les huitièmes de finale, où ils battent la Colombie (2-1 après prolongation). À cette occasion, Roger Milla, 38 ans, 
      rappelé in extremis en sélection, avant la Coupe du monde, inscrit deux nouveaux buts18. Le Cameroun devient 
      le premier pays africain à atteindre les quarts de finale d'une Coupe du monde. Au Stade San Paolo de Naples, 
      ils sont éliminés de justesse par l'Angleterre (2-3 après prolongation après avoir mené 2-1)</p>

