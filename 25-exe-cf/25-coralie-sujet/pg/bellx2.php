<p>
Le X-2 est développé afin d'étudier les caractéristiques du vol supersonique pour des vitesses supérieures à celles atteintes par ses prédécesseurs (Bell X-1 et Douglas Skyrocket). Le but du programme est aussi de récolter des données sur le phénomène d'échauffement cinétique dû au frottement de l'air sur la cellule d'un appareil.</p>
<p>
Le développement du projet est lent et difficile du fait des avancées, dans des domaines aussi variés que l'aérodynamique où la résistance des matériaux à haute température, nécessaires à la conception d'un appareil capable d'atteindre de telles vitesses. Le X-2 atteint des vitesses et des altitudes jamais vues auparavant et est un pionnier de la simulation informatique de la dynamique du vol. Il est aussi l'un des premiers appareil dont la poussée du moteur fusée peut être contrôlée.</p>
<p>
Concevoir des commandes de vol et une aérodynamique permettant à l'appareil d'atteindre des vitesses hautement supersoniques n'est que l'un des nombreux défis que l'équipe de recherche a relevé pour mener à bien le programme d'essai. Les ingénieurs savent qu'à l'approche de Mach 3, l'appareil va être soumis à ce qui est à l'époque nommé mur de la chaleur, c'est-à-dire un échauffement important de la cellule de l'appareil dû au frottement des molécules d'air sur la structure. Le X-2 est donc doté d'un revêtement à base d'acier inoxydable et d'un alliage de cuivre et de nickel baptisé K-Monel.
</p>
