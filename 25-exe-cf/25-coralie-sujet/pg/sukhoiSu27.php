<p>
Le Soukhoï Su-27 (Code OTAN Flanker) est un avion de chasse monoplace russe conçu par le bureau d'études Soukhoï (SDB) conduit par l'ingénieur Mikhaïl Simonov et son dirigeant historique Pavel Soukhoï. Il a donné naissance à de nombreuses variantes dont certaines ont reçu une nouvelle désignation : Su-30, Su-33, Su-35, Su-37.

En 2009, l'avion est en service dans les pays de l'ex-URSS et dans plusieurs autres pays, notamment en Afrique et en Asie (en particulier la République populaire de Chine sous la dénomination Shenyang J-11 et l'Inde). Le prix à l'exportation est d'environ 35 millions de dollars américains par avion, ou 70 millions de dollars avec un crédit de dix ans.
</p>
<em><h3>Conception</h3></em>
<p>
En 1969, les dirigeants de l'Union Soviétique ont lancé le programme PFI (Perspektivnyi Frontovoy Istrebitel), destiné à produire un avion de chasse supérieur à ceux des États-Unis et de l'OTAN (principalement le F-15 Eagle dont le développement venait de commencer).</p>
<p>
Après avoir étudié plusieurs possibilités, les ingénieurs de Soukhoï soumirent un projet définitif désigné T-10 en 1971. La configuration globale de l'appareil est relativement semblable à celle du F-15 américain : bi-réacteur, ailes hautes, double dérive, dimensions comparables. Grand et lourd, le Flanker est un avion extrêmement manœuvrable. Lors de démonstrations, les pilotes font régulièrement la figure appelée Cobra praticable avec une décélération dynamique et un angle d'attaque de 120°. Les pilotes de Su-27 ont créé de nouvelles figures très complexes et dangereuses, ce qui conduit quelquefois à des accidents, comme la collision entre 2 Su-27 appartenant aux Chevaliers russes, le 16 août 2009 près de Moscou2.</p>
<p>
Le prototype fit son premier vol le 20 mai 1977. Cependant, en raison de plusieurs défauts (poids trop élevé, réacteurs pas assez puissants, etc.), l'avion ne répondait pas aux espérances de l'armée de l'air soviétique (VVS). Après les accidents de deux prototypes sur quatre en juillet 1978, avec la mort des pilotes, le développement fut énormément ralenti. Ce n'est qu'après 1981 que la SDB construisit un nouveau prototype, le T-10S-1 (T-10-7, Flanker-B), qui n'avait plus grand chose en commun avec le T-10, effectuant son premier vol le 20 avril 1981. Cette version fut acceptée et le Su-27, dont la production en série débuta à Komsomolsk en 1983, entra en service en 1985, bien que les derniers problèmes ne fussent résolus qu'en 1990.</p>

<p>
Des Su-27 russes lors d'un exercice en Alaska en 2013.
À partir de 1986 un Su-27 modifié et désigné P-42 a battu 27 records de vitesse ascensionnelle et d'altitude entre 1986 et 1988, dont celui de l'altitude la plus élevée pour un chasseur. Certains étaient détenus par le F-15 Streak Eagle (version de records du F-15A, à ne pas confondre avec le F-15E Strike Eagle).</p>
<p>
Une variante navale a été testée pour la première fois le 17 août 1987, le Su-27T-10K (aujourd'hui le Su-33), avec ailes repliables, une crosse d'appontage et un train d'atterrissage renforcé. Cette version fut réceptionnée par l'aéronavale russe à partir de 1993.</p>

