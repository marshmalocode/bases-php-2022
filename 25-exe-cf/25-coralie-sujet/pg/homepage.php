<p>
Un avion1 est un aérodyne (un aéronef plus lourd que l'air), entraîné par un propulseurN 1, dont la portance aérodynamique est obtenue par des surfaces fixes. Lorsque la portance est obtenue (à l'arrêt ou en mouvement) par des surfaces en rotation, l'appareil est alors dit à « voilure tournante » (hélicoptère, autogire, girodyne).

Un avion équipé d'un dispositif lui permettant de décoller et de se poser sur l'eau (amerrir) est un hydravion. D'autres accessoires permettent l'atterrissage et le décollage sur des surfaces enneigées comme des skis situés sous les roues de l'avion.

Celui qui le dirige est appelé pilote ou aviateur.
</p>


<p>Le mot <em> « aviation » </em> (du latin « avis », qui signifie « oiseau »2, et du suffixe « atio ») a été employé pour la première fois par Gabriel de La Landelle, en 1863, dans le livre Aviation ou navigation aérienne sans ballon, un ouvrage rendant compte des tentatives d'envol de Jean-Marie Le Bris dans un appareil plus lourd que l'air.</p>
<br>
<p>
Le substantif masculin3,4,5 « avion » est un dérivé savant du latin avis6. Il est attesté au xixe siècle3 : d'après le Trésor de la langue française informatisé4, il a peut-être été créé en 1875 mais sa plus ancienne occurrence connue se trouve dans le brevet no BB 205 155, déposé le 19 avril 1890 par Clément Ader7 et relatif à « un appareil ailé pour la navigation aérienne dénommé Avion »8. C'est ainsi qu'Ader a appelé l'appareil baptisé Éole, avec lequel il décolle le 9 octobre 1890 puis rase le sol sur 50 mètres à 20 cm au-dessus de la piste. Cet événement ne sera toutefois pas homologué comme étant un vol : la hauteur atteinte était insuffisante pour le qualifier de tel.</p>
<br>
<p>
Le troisième prototype de Clément Ader, l'Avion III, effectue un vol de trois cents mètres devant un comité militaire le 14 octobre 1897 à Satory9. Une autre raison à la non-homologation des vols de Clément Ader est que ces vols étaient soumis au secret militaire.</p>
<br>
<p>
À la même époque Otto Lilienthal, grâce à des prototypes qui étaient réalisés à partir de structures de bambou entoilées de coton, pouvait planer jusqu'à 400 mètres en se lançant du haut d'une colline haute d'environ vingt mètres. Le contrôle de la machine se faisait par des déplacements du corps comme pour les deltaplanes pendulaires contemporains.</p>
<br>
<p>
Dans les premières années de l'aéronautique, après les vols en planeur des frères Wright de 1902 et leur premier vol motorisé du 17 décembre 190310[source insuffisante], on ne parle pas encore d'avions mais d'aéroplanes. En 1908, Ferber, dans une note de bas de page de son ouvrage L’aviation, ses débuts, son développement11, écrit « Il n'y a pas de mot pour désigner l'aéroplane en particulier ; on pourrait prendre le nom créé par M. Ader ». En 1911, en hommage à Clément Ader, le général Roques, créateur de l'aviation militaire, décide que tous les aéroplanes militaires s'appelleront des avions. Mais ce n'est qu'avec la Première Guerre mondiale que les mots « avion » et « aviation » deviennent communs.</p>
<br>
<p>
Alberto Santos Dumont construisit de nombreux ballons à bord desquels il vola et conçut le premier dirigeable pratique. La démonstration de son aéroplane plus-lourd-que-l'air, le 14-bis, eut lieu dans le parc de Bagatelle près de Paris, avec un vol public, homologuant par là même le premier record du monde d'aviation, le 23 octobre 1906.</p>
	

