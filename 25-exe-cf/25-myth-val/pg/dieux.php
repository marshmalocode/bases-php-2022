    <h1>Juste pour se faire plaisir</h1>


    <p>Au delà des dieux très connus tel que Zeus, Odin ou Anubis, les peuples vouaient également un culte à des dieux mineurs un peu plus spécialisés et parfois dans des domaines vraiment... exotiques.</p>
    <br>
    <h3>Carna, déesse romaine des gonds</h3>
        <p>Carna, jeune Nymphe effarouchée vivant dans un bois sacré au bord du Tibre, aimait se promener, séduire les promeneurs et ensuite les semer dans la forêt quand ceux-ci lui courraient après. Jusqu’au jour où elle voulu faire son petit numéro à un type qui passait par là - sauf que le type en question était Janus, le dieu aux deux visages. Grâce à ses yeux dans le dos, il la repéra facilement et lui fit sa petite affaire. En échange de sa virginité arrachée de force, il lui confia la responsabilité charnière d’être déesse des gonds. Elle pouvait ouvrir ce qui était fermé et fermer ce qui est ouvert...</p>

    <h3>Matshishkapeu, l'esprit amérindien qui s'exprime à travers les pets</h3>
        <p>Le peuple Innu, peuple autochtone du Québec et du Canada, vénérait de nombreux esprits qu’ils prenaient pour des dieux. Ces esprits s’adressaient aux hommes pendant les songes, les rêves, à travers certaines personnes comme les sorciers, ou encore dans des roulements de tambours.<br>
        Cependant Matshishkapeu a décidé de s’exprimer à travers les flatulences. C'était un dieu très puissant.<br>
        Les hommes cherchaient à interpréter les paroles de cet esprit mystérieux, et avaient pour habitude de débattre entre eux afin de pouvoir comprendre le mieux possible quel était le message, en analysant le bruit et les odeurs de leurs pets. </p>

    <h3>Sterquilinus, dieu romain du fumier et des matières fécales en général</h3>
        <p>Les Romains avaient en effet un dieu dédié aux toilettes et aux excréments. On n'ose pas tropimaginer à quoi pouvait ressembler ses temples.<br>
        Il était néanmoins indispensable pour l’agriculture car il fournissait un engrais de choix.<br>
        On retrouve ce type de dieux dans de nombreuses civilisations comme le dieu babylonien Shed Bet ha-Kise, ou encore la divinité chinoise Zigu.</p>

    <h3>Hel, déesse scandinave des morts sans héroïsme</h3>
        <p>La différence entre une bonne mort et une mauvaise mort ? Pour les Vikings c’est simple, il faut mourir l’arme à la main pour avoir une mort digne de ce nom.<br>
        Sinon c'est que vous avez une « mort de paille »: c'est-à-dire une mort naturelle. Sans gloire quoi.<br>
        Et pour les morts sans gloire, il y a une déesse particulière:Hel. Régnant sur neuf mondes infernaux, elle a pour seuil la Perfidie, pour lit la Maladie, pour écuelle la Disette et pour couteau la Faim.<br>
        Son nom est à l'origine des mots hell en anglais, Hölle en allemand et helvete dans les langues scandinaves, qui sous l'influence du vocabulaire ecclésiastique, ont pris le sens d'enfer. </p>


    <h3>Xochipilli, dieu aztèque de la défonce et des gigolos</h3>
        <p>Le Prince des Fleurs. Et quand on dit fleurs, on parle bien de toutes les fleurs: les jolies, les moins jolies et les étranges. Inclus doonc les plantes hallucinogènes et les champis. Sympa le dieu. <br>
        Xochipilli c’est aussi le dieu de l’amour - le dieu de l’amour physique et celui de l'amour monnayé. Le dieu des gigolos quoi. Mais attention: il punissait ceux qui s’étaient rendus coupables de certaines transgressions sexuelles en les affligeant d’hémorroïdes ou de maladies vénériennes.</p>

    <h3>Terme, dieu des bornes kilométriques et des limites</h3>
        <p>il est vrai que les Romains étaient célèbres pour avoir construit des kilomètres et des kilomètres de routes. Et sans péage et aire de repos qui puent la pisse, ce qui est plutôt honorable. Il y avait donc un dieu romain qui veillait sur les bornes placées au bord de la route, nommées les termini en son honneur. Lourde responsabilité. Si vous aviez le malheur de déplacer une borne, ou d’en piquer une pour meubler votre salon, la population avait le droit de vous traquer et de vous tuer. Allez tout de suite reposer cette borne de la D32 qui prend la poussière dans votre entrée où vous l’avez trouvée. </p>
    
    <h3>Saehrimnir, le cochon-dieu des Scandinaves</h3>
        <p>Les Vikings se disent par soucis d’équité, que leur bouffe aussi avait le droit à une divinité. Ils ont donc décidé d’inclure au panthéon nordique Saehrimnir, le dieu jambon. <br>
        Saehrimnir avait une vie pas super rigolote. Tous les matins les dieux le traquaient, finissaient par l’attraper et le faisaient bouillir pour se faire une grosse bouffe de sanglier sacré. À la fin du festin, le pauvre Saehrimnir renaissait de ses restes, et repartait gambader dans la forêt en tentant d’échapper à ses tortionnaires. Qui forcément finissaient par se le farcir de nouveau.</p>

    <h3>Tlazolteotl, déesse aztèque "mangeuse de saleté"</h3>    
        <p>Son surnom de mangeuse de saleté lui vient du fait que Tlazolteotl était la déesse qui mangeait toutes les impuretés des mourants, avant de bouffer leur corps complètement. Elle était notamment scatophile, c’est pourquoi elle s’entendait bien avec Sterquilinus.<br>
        Selon d’autres sources son surnom lui aurait été donné à cause de ses habitudes sexuelles buccales avec le dieu Quetzalcoatl. Déesse de la pipe donc.</p>

    <h3>Lofn, déesse des amours perdus et illégitimes</h3>
        <p>Encore une divinité nordique. Les Vikings, bonnes grosses brutes sanguinaires, aimaient par dessus tout la baston et les femmes. Concernant la baston, il y avait ce qu’il fallait en matière de dieux. Mais pour les femmes, et en particulier pour les relations extra-conjugales, les Vikings avaient besoin d’un prétexte et d’un truc qui leur laisse bonne conscience après un petit viol des familles. Du coup c’est pratique, on a la religion pour ça. Lofn permettait ainsi le rapport entre un homme et une femme à qui le mariage était interdit. Lofn était une déesse qui, bizarrement, était particulièrement appréciée des hommes.</p>

    <h3>Le dieu inconnu, pour les laissés pour compte</h3>
        <p>Les Grecs, avec leur panthéon monstrueux, avaient cependant peur d’avoir oublié quelqu’un. Un corps de métier, un animal ou un arbre pas trop courant qui n’aurait pas de dieu à lui. Horreur. <br>
        Pour pallier leurs potentiels oublis et éviter de vexer les dieux, les Grecs ont créé le dieu inconnu, pour toutes ces espèces ou institution sans dieu. Juste par sécurité. </p>

    <h3>Vertumne</h3>
        <p>Emblème de l'année, de ses variations et de l'inconstance des choses. Son nom vient d'un mot latin qui signifie "tourner, changer."</p>

