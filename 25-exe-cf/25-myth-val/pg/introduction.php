        <h2>Qu'est-ce que la mythologie?</h2>
            <p>La mythologie (du grec μυθολογία / muthología, de μῦθος / mûthos, « parole », et λόγος / lógos, « discours ») est soit un ensemble de mythes liés à une civilisation, une religion ou un thème particulier, soit l'étude de ces mythes. Les chercheurs qui étudient les mythologies sont appelés « mythologues ».<br>
            Comprise comme ensemble de mythes, la notion de mythologie est généralement utilisée pour décrire des ensembles de récits et de figures divines, humaines ou monstrueuses brassés par les systèmes religieux des civilisations anciennes ou de sociétés traditionnelles, éloignées dans l'espace ou dans le temps.
            <br>
            Comprise comme l'étude des mythes, la mythologie remonte également à l'Antiquité, dans la mesure où les Grecs anciens portent très rapidement un regard critique sur leurs propres mythes, ce qui amène à des interprétations liées à une volonté de réécriture réaliste ou moralisante, via des courants tels que l'évhémérisme et la pratique du commentaire allégorique. Mais ce n'est qu'au XIXe siècle que les études mythologiques se constituent en une discipline à prétention scientifique, dans le contexte du développement des sciences sociales, en particulier de l'anthropologie. C'est aussi à ce moment que naît la mythologie comparée, conçue d'abord sur le modèle de la linguistique comparée. De cette évolution sont issus les principaux courants des études mythologiques aux XXe – XXIe siècles, tels l'interprétation ritualiste, l'approche d'inspiration psychanalytique ou structuraliste. L'anthropologue Claude Lévi-Strauss, appliquant sa méthode structurale à l'analyse des mythes, a développé dans les années 1960 une approche originale pour l'époque, d'inspiration holiste et cognitiviste.</p>

        <h3>La mythologie comme ensemble de mythes</h3>

            <p>Tout comme la notion de mythe, le terme « mythologie » provient de la Grèce et a d'abord été utilisé dans le contexte de la culture grecque ancienne. Par la suite, les deux notions ont été appliquées à toutes sortes de cultures parfois radicalement différentes. Cela peut poser des problèmes de méthode lorsqu'on étudie ces cultures, dans la mesure où l'emploi de ces notions revient à supposer d'emblée que l'ensemble des religions, cultes et récits ancestraux fonctionnent exactement de la même façon que ceux de la mythologie grecque, alors que des études plus attentives ont révélé souvent des différences profondes d'une culture à l'autre1. De plus, on parle généralement des mythologies au pluriel : la question de savoir dans quelle mesure on peut les rassembler sous un concept unifié est un problème complexe, qui relève de la mythologie comparée.
            Les mythologies du monde
