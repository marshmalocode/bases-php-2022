    <h1>L'histoire des Pays-Bas</h1>
    <div class="text">
        <p>
        Peuplés de tribus celtes puis germaniques dont les Frisons et les Bataves, les Pays-Bas du Nord échappent ou résistent à la domination de l'Empire romain établi dans le Sud (→ histoire de la Belgique) et dont la frontière septentrionale est alors formée par le Rhin.</br></br>
        
        <span>IX-XVe siècles </span></br></br>

        Au Moyen-Âge, ces territoires sont intégrés à l’empire carolingien puis « romain-germanique » et christianisés. Ils se fractionnent cependant en de multiples seigneuries féodales, dont le comté de Hollande qui apparaît dès le xie siècle.</br></br>
        
        <span>XVe siècle</span></br></br>
    
        Les Pays-Bas forment la partie septentrionale des États bourguignons et sont soumis à une première et partielle centralisation sous le règne de Philippe le Bon (1419-1467).</br></br>
    
        <span>XVIe siècle</span></br></br>
    
        Les Habsbourg en héritent et Charles Quint puis son fils Philippe II, resserrent l’emprise de l’Espagne sur l’ensemble des provinces, portées à dix-sept au sein du « cercle de Bourgogne ». L'emprise des Habsbourg suscite, en particulier dans le Nord acquis à la Réforme protestante, une opposition croissante qui entraîne, sous la direction de Guillaume d’Orange, une révolte générale (1568). À l’issue de la guerre de Quatre-Vingt ans, l’indépendance des Provinces-Unies, réunissant, depuis l’Union d’Utrecht (1579), sept provinces septentrionales (Gueldre, Hollande, Zélande, Utrecht, Frise, Overijssel et Groningue), est officiellement reconnue par l'Espagne en 1648.</br></br>
    
        <span>XVIIe siècle, le « Siècle d’or » néerlandais</span></br></br>
    
        Devenue une puissance à la fois militaire et commerciale, se dotant d’un vaste empire colonial placé en grande partie sous le contrôle de la Compagnie des Indes orientales, la république des Provinces-Unies connaît un exceptionnel essor économique et culturel.</br></br>
    
        <span>XVIII-XIXe siècles</span></br></br>
    
        Après l’expérience révolutionnaire (République batave créée en 1795), la restauration de la monarchie en 1815 et la réunification du Nord et du Sud jusqu’à l’indépendance de la Belgique en 1830, le royaume des Pays-Bas évolue vers le parlementarisme (Constitution de 1848). Les divisions religieuses – entre protestants et catholiques – et sociales, ainsi que la question scolaire, donnent naissance à des partis politiques spécifiques.</br></br>
    
        <span>Du XXe siècle à nos jours</span></br></br>
    
        Pendant la Seconde Guerre mondiale, les Pays-Bas subissent l'occupation allemande. En 1949, leur grande colonie, l'Indonésie, obtient son indépendance.

        Sur le plan politique, à mesure qu’il se démocratise (suffrage universel, en 1917-1918) le régime parlementaire se consolide avec l’institution d’un système politique original fondé à la fois sur le cloisonnement par « piliers socioculturels et politiques » et le consensus entre élites gouvernementales. Un modèle de démocratie remis en cause, à partir des années 1970, avec l’apparition de nouveaux partis et, depuis la fin des années 2000, l’affirmation d’une droite populiste.</p>
