        <p>Comme pour bien d'autres pays du continent africain, l'écriture de cette histoire se heurte à de multiples difficultés2, tant méthodologiques qu'idéologiques.</p>

        <p> Les commentaires attribués à certains explorateurs antiques3, les manuscrits des voyageurs arabes constituent les premiers témoignages connus4. C'est à partir du xve siècle que l'on dispose de sources européennes5 – narrations, compilations ou documents d'archives –, dont la finalité n'était généralement pas historique au départ : le chevalier de Boufflers décrit son exil à madame de Sabran, le naturaliste Adanson détaille la flore et la faune du pays, le directeur de la Compagnie du Sénégal Durand relate les péripéties de son expédition et l'abbé Boilat, dans ses Esquisses sénégalaises, pense surtout à l'évangélisation de la contrée.</p>



        <p> Tantôt naïf, tantôt condescendant, le colonisateur se veut à la fois historien, ethnographe et pédagogue, comme en témoignent les ouvrages publiés par les administrateurs de l'AOF, les « villages noirs »6 ou l'Exposition coloniale de 1931. Devant leurs réalités ainsi figées et dénaturées, les populations se voient privées de leur propre histoire. En outre la non-reconnaissance de leur participation à l'effort de guerre alimente une prise de conscience croissante à partir des années 1950, qui trouve un écho dans la déclaration d'indépendance en 1960, mais aussi dans la pensée novatrice de l'historien et anthropologue sénégalais Cheikh Anta Diop, chef de file de ce qu'on appellera l'« École de Dakar »7. Dans l'intervalle les recherches archéologiques8, dynamisées sous l'égide de l'IFAN par Raymond Mauny et d'autres chercheurs européens, ont été relayées par des spécialistes nationaux et le Sénégal compte aujourd'hui nombre d'historiens de premier plan, tels que Abdoulaye Ly, Boubacar Barry, Abdoulaye Bathily, Hamady Bocoum, Iba Der Thiam, Mamadou Diouf ou Oumar Kane, disparu en 2008.</p>

        <p> L'écriture de l'histoire du Sénégal implique aussi un combat contre la montre, car le patrimoine est toujours sous la menace du climat, des insectes9 ou de la pénurie budgétaire. Sa sauvegarde reste préoccupante d'un point de vue scientifique, sans parler des enjeux touristiques dans le cas de Saint-Louis, Gorée ou Karabane.

            Les traditions orales – désormais reconnues patrimoine oral et immatériel de l'humanité par l'UNESCO – sont un peu mieux prises en compte, comme en témoigne ce colloque international consacré aux traditions orales du Kaabu en 1980 à Dakar10, mais n'ont pas encore trouvé une vraie place dans la construction du savoir à l'occidentale.</p>


