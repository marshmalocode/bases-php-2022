        <h2>La culture en général</h2>
        <p>La culture du Sénégal, en Afrique de l'Ouest, désigne d'abord les pratiques culturelles de ses 17 000 000 d'habitants au début des années 2020.

            Des villages les plus isolés aux lieux branchés de Dakar la capitale, la culture sénégalaise se caractérise notamment par son goût pour les sports traditionnels, la musique, la danse, le vêtement, le travail créatif et ingénieux de tout matériau à portée de main, les moments de convivialité liés aux repas et les fêtes, sans oublier – de l'arbre à palabres aux téléphones mobiles –, un attrait indéniable pour l'échange et la communication orale.</p>

        <img src="img/fautlion.jpg" class="img" alt="img">
        <p>A l’origine, le faux-lion est un rite de possession. Il remonte à l’époque où le Sénégal était couvert d’épaisses forêts peuplées d’animaux sauvages comme les lions, les hyènes, les singes, les chacals, les gazelles. On raconte que le chasseur qui avait été attaqué par un lion et avait survécu devenait une personne étrange. Choqué par sa rencontre, il perdait la tête, il rugissait comme un lion, ne mangeait que de la viande crue, des poils lui poussaient sur le corps. Il était le lion. Pour le soigner, les guérisseurs procédaient alors à des rituels de « possession », tels qu’on les voit encore aujourd’hui dans les cas de possession par un esprit ancestral.</p>
        <p>Des hommes se déguisent en lions (faux lions) avec un maquillage imitant le fauve : le rouge et le noir, des visages barrés de moustaches et un aspect terrifiant. Ils arrivent, annoncés par des tam-tam, rugissant, cachés au public par des pagnes, à la place prévue où les attendent déjà organisateurs et spectateurs dans leurs habits de fête.</p>

        <img src="img/lutte.jpg" class="img" alt="img">
        <p>La lutte sénégalaise (« Laamb en wolof ») est un sport populaire, traditionnel venant du Sénégal. En effet, elle se déroule en particulier dans les régions de la Casamance, Ziguinchor et du Sine-Saloum. Intégrant des techniques de la boxe, on la surnomme la « lutte avec frappe ». Ce sport regroupe à la fois une dimension physique, spirituelle, culturelle et folklorique (bakk).</p>
        <p>La lutte était autrefois une fête rythmique qui intervenait après une récolte abondante chez les Sérères, après une bonne pêche chez les Lébous. Elle permettait, tout en se divertissant, de mesurer la valeur et l’adresse des guerriers. Des tournois de lutte étaient organisés pour déterminer le champion du village entre les localités voisines.

            La lutte traditionnelle a fait place à la lutte avec frappe, qui s’est professionnalisée. D’importants investissements sont réalisés pour entraîner les lutteurs à un niveau international. Les écuries se sont progressivement substitués aux villages.</p>

