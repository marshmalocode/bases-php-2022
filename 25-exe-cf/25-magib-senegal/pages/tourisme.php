        <h1 class="titre">Nous vous présentons une visite guidé des zones touristique du Sénégal. </h1>
        <img src="img/goree.jpg" class="img" alt="img">
        <p>Découverte en 1444 par des marins portugais sous le commandement de Denis Dias, l’île fut baptisée « Palma » mais elle est également nommée « Beseguiche » dans des documents d’époque, alors que les populations locales l’appelaient « Bir » ou « Ber« La marine hollandaise s’en saisit en 1588 qui la rebaptisa »Goede reede » (= bonne rade) d’où son nom de Gorée.</p>

        <p>Reconnu officiellement par l’Organisation des Nations unies (ONU) en 1978 : Gorée, « île-mémoire », fut l’un des tout premiers lieux à être portés sur la liste du patrimoine mondial gérée par l’Organisation des Nations unies pour l’éducation, la science et la culture (UNESCO). Située à 4 kilomètres au large de Dakar, à vingt minutes par la chaloupe, l’Île de Gorée est un des endroits les plus attachants du Sénégal. Elle se caractérise par le témoignage exceptionnel qu’elle apporte sur l’une des plus grandes tragédies de l’histoire des sociétés humaines : la traite négrière. Les différentes unités de cette « île mémoire » — forts, bâtisses, rues, places, etc. — racontent, chacune à sa manière, l’histoire de Gorée qui a été du XVe au XIXe siècle le plus grand centre de commerce d’esclaves de la côte africaine.</p>

        <img src="img/Lompoul.jpg" class="img" alt="img">
        <p>Petite étendue désertique d’une superficie de 18 km2, le désert de Lompoul est situé aux confins de l’océan Atlantique, dans le Nord-Ouest du Sénégal entre Dakar, la capitale du pays de la Teranga, et Saint-Louis. Il est formé de dunes de sable atteignant parfois près de 50 mètres où la végétation est quasi inexistante. A sa proximité se trouvent Lompoul village et la ville de Kébémer.</p>

        <p>Le désert de Lompoul peut se découvrir à pied, dans le cadre d’une randonnée. Les amateurs de sensations fortes pourront explorer ses dunes de sable et rejoindre la plage à bord d’un 4×4. Les voyageurs en quête d’authenticité, quant à eux, opteront plutôt pour une escapade jusqu’au cœur du désert à dos de dromadaire. Dans tous les cas, quel que soit le mode de transport que vous utilisez, un panorama à couper le souffle s’offre à ceux qui s’aventurent au sommet des impressionnantes dunes de sable.</p>

        <img src="img/lac_rose.webp" class="img" alt="img">
        <p>Le lac rose était autrefois rattaché à l’océan atlantique. Avec le temps, l’installation de la sécheresse a favorisé sa séparation puis son éloignement de cet océan par une accumulation de dunes de sable. Ces dernières sont depuis colonisées par des palmiers et des filaos. A 45 kilomètres de Dakar, vers le Nord, une des merveilles du Sénégal : un lac à la couleur bien particulière, un produit de la "pêche" tout aussi original et de superbes oiseaux.

            Le Lac Rose appelé aussi Lac Retba a été rendu célèbre dans le monde entier par le rallye Paris-Dakar dont il constitue l'ultime étape.</p>

        <p>Ce lac a des caractéristiques particulières : par un phénomène inexpliqué il est devenu, il y a maintenant 40 ans, rose. Et oui ! Selon les heures sa couleur vire du rose au mauve exactement comme sur les cartes postales. Or cette couleur n'est pas seulement un effet d'optique : en prenant l'eau dans votre main, l'eau est toujours rose. Alors pourquoi ?



            Cette couleur est en faîte due à la présence au fond du lac d'une algue microscopique qui oxyde le fer de l'eau salée. La couleur est plus impressionnante quand le soleil est au zénith et pendant la saison sèche. Un beau spectacle à ne pas manquer ! La "Mer morte" du Sénégal.</p>

        <img src="img/sine_saloum.jpg" class="img" alt="img">

        <p>Le Sine Saloum prend son nom du fleuve Saloum et de son principal affluent, le Sine. Elle constitue plus de 70 % du bassin arachidier. Elle abritait jadis les royaumes sérères du Sine (capitale Diakhao) et du Saloum (capitale Kahone).</p>

        <p>Le delta du Sine Saloum (parc national) est l’un des plus beaux sites du Sénégal. D’une superficie d’environ 180 000 hectares, c’est une zone constituée de mangroves, de lagunes, de forêts et de cordons sableux.
            Elle est située au centre et au sud du bassin arachidier et présente une grande diversité d’écosystèmes naturels. Elle est arrosée par le fleuve Saloum, le Baobolong, un affluent du fleuve Gambie, le Niani Nja bolong, l’affluent de la Sandugou et le Miniminiyang bolong.
        </p>

        <p>Les villages de pêcheurs du littoral et ceux installés le long des méandres des fleuves Sine et Saloum sont des sites touristiques appréciés. La pêche, réalisée de manière artisanale, y est abondante.

            Sur la pointe de Sangomar, entre fleuve et océan, les villages de Palmarin et de Djiffer. En face, accessibles par pirogue, les îles de Niodior et de Dionewar.

            Au nord du delta, la petite ville de Ndangane, sur un bras du fleuve Saloum, qui donne accès à l’île de Mar Lodj où des campements présentant une architecture de style local sont proposés aux visiteurs.</p>

        <img src="img/SAINT-LOUIS1.jpg" class="img" alt="img">

        <p>Située au bord de l’Océan Atlantique et à la jonction du désert du Sahara avec la brousse subsaharienne, Saint-Louis du Sénégal est certainement la meilleure introduction à la découverte de l’Afrique de l’Ouest francophone.</p>
        <p>Plus ancienne ville édifiée par les français en Afrique de l’Ouest (1659), symbole de l’élégance et du raffinement, Saint-Louis fut la capitale du Sénégal et de l’Afrique Occidentale Française au début du XXième siècle.</p>

        <p>Grâce à son patrimoine architectural unique, l’île Saint-Louis est depuis l’an 2000 classée au patrimoine mondiale de l’humanité (UNESCO)…</p>

        <p>De même, deux de ses parcs naturels : le Parc de la Langue de Barbarie et le Parc aux oiseaux du Djoudj sont depuis plus de 15 ans classés au patrimoine mondiale de l’humanité (UNESCO).
        </p>

        <img src="img/casamance-1024x555-1.webp" class="img" alt="img">
        <p>La région naturelle de la Casamance a des limites qui tiennent à la fois de la nature et de l’histoire. Elles sont à l’Ouest l’océan Atlantique, à l’Est la rivière Kuluntu (affluent du fleuve Gambie), au Nord la Gambie, et au Sud la Guinée Bissau.</p>

        <p>La Casamance représente 28 350 kilomètres carrés soit 1/7 de la superficie du Sénégal (presque comme la Belgique). La région est étroite et allongée d’Est en Ouest de part et d’autre d’un fleuve de 300 km qui donne son nom à la région.</p>
        <p>La vie sociale et économique s’organise autour du fleuve Casamance et de ses nombreux bolongs et lagunes formant un labyrinthe. Palmeraies, forêts et mangroves tapissent les îles portant des rizières.</p>





