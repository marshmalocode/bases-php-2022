    <h2>Apprenez à gérer votre espace</h2>
        <p>Cuisiner demande de l’espace, logique, mais il faut également apprendre à le maîtriser. Je m’explique, beaucoup de personne ont la mauvaise habitude de croire que pour bien cuisiner il faut une grande cuisine bien équipée.</p>
        <p>Pour ma part, je pense que plus votre cuisine est grande, et plus vous aurez envie de vous étaler de partout et d’être désorganisé. De plus, plus la distance sera grande entre votre poste de travail, le frigo, l’évier, et votre plaque de cuisson, plus vous perdrez du temps à aller de l’un à l’autre et moins vous serez efficace. </p>
        <p>À l’inverse, dans une toute petite cuisine, vous n’avez pas le choix. Vous devez être organisé, ranger, nettoyer au fur et à mesure, parce que sinon vous ne pouvez plus bouger. Le mieux c’est d’avoir assez d’espace pour vous mouvoir en ayant tout à portée de main à 1 ou 2 pas maximum. Donc si vous avez une très grande cuisine, réunissez le matériel et les ingrédients nécessaires à la recette, délimitez un espace et restez-y. <strong> Savoir gérer son espace permet de cuisiner dans de meilleures conditions, de gagner du temps et d’y voir clair !
        </strong> Plus il y a de bazar plus, il est difficile de cuisiner.</p>
        <p>Si vous avez une petite cuisine, essayez de trouver un espace ailleurs, comme votre table à manger. Vous pourrez y faire toutes les préparations préliminaires (éplucher, découpe des légumes ou de la viande, préparation de la pâte, etc.). Votre coin cuisine servira uniquement pour les cuissons.</p>

